class CapDevPipeInstaller:
    """Installs and configures cap-dev-pipe into a target project (TUI-agnostic).

    Construct with no arguments and drive via :meth:`execute` / :meth:`verify` /
    :meth:`apply_mode`, passing an :class:`InstallConfig`. All file/shell work happens
    here; the TUI handler only gathers inputs and renders results.
    """

    def __init__(self) -> None:
        self.logger = logger

    # -- platform ---------------------------------------------------------- #

    @staticmethod
    def _symlinks_available() -> bool:
        """Whether symlinks are usable on this platform (FR-4 / D-9).

        On Windows, symlinks need admin/dev-mode, so the flow defaults to / forces copy
        for both scripts and profile docs. Patchable in tests to exercise the fallback.
        """
        return os.name != "nt"

    def _resolve_profile_method(self, cfg: InstallConfig) -> InstallMethod:
        """The method used for profile docs: explicit override, else cfg.method, forced
        to COPY when symlinks are unavailable (FR-9 / D-9)."""
        chosen = cfg.profile_method or cfg.method
        if chosen is InstallMethod.SYMLINK and not self._symlinks_available():
            return InstallMethod.COPY
        return chosen

    # -- diagnosability (NFR-8) -------------------------------------------- #

    def _log_action(self, action: Action) -> None:
        """Log a planned/applied action for a durable trail (NFR-8)."""
        self.logger.info(
            "capdevpipe action: %s",
            action.describe(),
            extra={
                "pipeline_name": "capdevpipe_install",
                "action_type": action.type.value,
            },
        )

    def _run_subprocess(
        self,
        argv: Sequence[str],
        cwd: Optional[Path] = None,
        timeout: Optional[float] = None,
    ) -> subprocess.CompletedProcess:
        """Run a subprocess, logging the invocation and captured output (NFR-8).

        Used for the copy installer (FR-6) and verification (FR-11). Output is captured
        (text) and logged so a field failure leaves a trail beyond the on-screen message.
        Raises :class:`FileOperationError` if the executable cannot be launched.
        """
        argv = [str(a) for a in argv]
        self.logger.info(
            "capdevpipe subprocess: %s (cwd=%s)",
            " ".join(argv),
            cwd,
            extra={"pipeline_name": "capdevpipe_install"},
        )
        try:
            proc = subprocess.run(
                argv,
                cwd=str(cwd) if cwd else None,
                capture_output=True,
                text=True,
                timeout=timeout,
            )
        except (OSError, subprocess.SubprocessError) as exc:
            raise FileOperationError(
                f"Failed to run {argv[0]!r}: {exc}. Check the file exists and is executable.",
                file_path=argv[0],
                original_error=exc,
            ) from exc
        self.logger.info(
            "capdevpipe subprocess exit=%s stdout=%d bytes stderr=%d bytes",
            proc.returncode,
            len(proc.stdout or ""),
            len(proc.stderr or ""),
            extra={"pipeline_name": "capdevpipe_install"},
        )
        if proc.stderr:
            self.logger.debug("capdevpipe subprocess stderr: %s", proc.stderr.strip())
        return proc

    # -- source / target (FR-2, FR-3) -------------------------------------- #

    def locate_source(self, override: Optional[Path] = None) -> Path:
        """Locate and validate the canonical cap-dev-pipe checkout (FR-2).

        Defaults to :data:`DEFAULT_SOURCE`; ``override`` takes precedence. Validates the
        directory has the :data:`SOURCE_MARKERS`. Raises :class:`ConfigurationError` with
        the path and a remediation if it is missing or not a real checkout.
        """
        source = (Path(override) if override else DEFAULT_SOURCE).expanduser()
        if not source.is_dir():
            raise ConfigurationError(
                f"cap-dev-pipe source not found at {source}. Point to a valid cap-dev-pipe "
                f"checkout (clone it or pass the correct source path)."
            )
        missing = [m for m in SOURCE_MARKERS if not (source / m).exists()]
        if missing:
            raise ConfigurationError(
                f"{source} is not a valid cap-dev-pipe checkout (missing: "
                f"{', '.join(missing)}). Point to the cap-dev-pipe repository root."
            )
        if not (source / "pipeline" / "embed_manifest.py").is_file():
            raise ConfigurationError(
                f"{source} is missing pipeline/embed_manifest.py. Point to a cap-dev-pipe "
                f"checkout with modular embed support (Increment A+)."
            )
        return source.resolve()

    def _validate_target(self, cfg: InstallConfig) -> None:
        """Validate the target project root (FR-3): a directory, never the source tree."""
        target_path = Path(cfg.target_root)
        if not target_path.is_dir():
            raise ValidationError(
                f"Target project root is not a directory: {cfg.target_root}. "
                f"Choose an existing project directory.",
                field="target_root",
                value=str(cfg.target_root),
            )
        target = Path(os.path.realpath(target_path))
        source = Path(os.path.realpath(cfg.source_path))
        if target == source or source in target.parents:
            raise ValidationError(
                f"Refusing to install into the cap-dev-pipe source tree ({target}). "
                f"Choose a separate target project directory.",
                field="target_root",
                value=str(target),
            )

    # -- planning / execution (FR-13, FR-16) ------------------------------- #

    def _effective_method(self, cfg: InstallConfig) -> InstallMethod:
        """The method actually used: forced to COPY when symlinks are unavailable (FR-4 / D-9).

        On Windows (no reliable symlinks) a requested ``symlink`` install falls back to
        ``copy`` for **scripts** as well as profile docs — the installer enforces this so a
        caller cannot accidentally request an unsupportable symlink install.
        """
        if cfg.method is InstallMethod.SYMLINK and not self._symlinks_available():
            return InstallMethod.COPY
        return cfg.method

    def plan_actions(self, cfg: InstallConfig) -> List[Action]:
        """Compute the ordered, idempotent action list (FR-13 preview / FR-16 source).

        This is the *single* source of writes for :meth:`execute`, :meth:`verify`, and
        rollback — the same list the TUI previews is the list ``execute`` consumes
        (R2-F4). Each action is replayable (R2-S3). Composed from the per-concern builders
        (tasks #4–#7).
        """
        self._validate_target(cfg)
        # Namespace guard (FR-A8): refuse to embed a `pipeline` package into a project that
        # already has a generic `pipeline` module which would shadow it. Delegated to canonical.
        check_embed_namespace(Path(cfg.source_path), cfg.embed_profile, cfg.target_root)
        actions: List[Action] = []
        if self._effective_method(cfg) is InstallMethod.SYMLINK:
            actions += self.embed_symlink(cfg)
            actions += self.write_pipeline_env(cfg)
        else:
            # Copy path: rsync writes scripts/design/prompts/pipeline.env; reconcile fixes
            # the four managed keys + the auto-linked root java profile (FR-6).
            actions += self.embed_copy(cfg)
            actions += self.reconcile_copy_install(cfg)
        actions += self.generate_wrapper(cfg)
        for profile in cfg.profiles:
            actions += self.create_profile(cfg, profile)
        actions += self.update_gitignore(cfg)
        return actions

    def execute(
        self, cfg: InstallConfig, actions: Optional[List[Action]] = None
    ) -> ExecuteResult:
        """Run the planned actions in order, confined to ``target_root`` (FR-16, NFR-6).

        Applies each action idempotently (skipping those already satisfied) and writes the
        install manifest on success. On failure: the symlink path rolls back its own writes;
        a path containing an external subprocess (copy/rsync) cannot be cleanly reversed, so
        it returns a repairable state instead (R2-S2).

        **Preview fidelity (FR-13 / FR-16 — R4-F5).** Pass the *already-computed* preview list
        as ``actions`` so the list the TUI showed is the exact list executed — the "single
        planned action list" the requirement mandates. When ``actions`` is ``None`` the plan is
        computed here (headless/library callers that skip the preview). Either way the set is
        deterministic, but consuming the preview removes the recompute-and-hope gap.

        Refuses to layer onto a prior **pending** (crashed/incomplete) install (R2-S7): the
        caller should run ``apply_mode(... REPAIR)`` instead. TOCTOU symlink confinement
        (R2-S5) and the pending/complete crash marker are enforced here and in
        :meth:`_run_actions`.
        """
        if self.detect_existing(cfg.target_root).pending:
            return ExecuteResult(
                success=False,
                repairable=True,
                error=(
                    "A prior run left an incomplete (pending) install. Re-run and choose "
                    "'repair' to complete it safely rather than layering a second install."
                ),
            )
        if actions is None:
            actions = self.plan_actions(cfg)
        return self._run_actions(actions, cfg)

    # -- execution engine (private) ---------------------------------------- #

    def _assert_embed_not_escaping(self, cfg: InstallConfig) -> None:
        """Refuse if ``.cap-dev-pipe/`` is a pre-existing symlink escaping the target (R2-S5).

        A planted/accidental ``target/.cap-dev-pipe -> /elsewhere`` would otherwise redirect
        every write outside the target. Per-action confinement also catches this mid-run; this
        pre-flight refuses upfront with a clear message.
        """
        embed = Path(cfg.target_root) / EMBED_DIR_NAME
        if embed.is_symlink():
            resolved = Path(os.path.realpath(embed))
            root = Path(os.path.realpath(cfg.target_root))
            if resolved != root and root not in resolved.parents:
                raise FileOperationError(
                    f"Refusing to install: {embed} is a pre-existing symlink to {resolved}, "
                    f"outside the target. Remove it and re-run.",
                    file_path=str(embed),
                )

    def _confine(self, path: Path, target_root: Path) -> None:
        """Refuse a write whose containing directory escapes ``target_root`` (NFR-6, basic).

        Resolves the **parent** directory (where the entry is created), not the entry itself:
        a script symlink legitimately points at an absolute source path *outside* the target
        (NFR-3), so following the leaf would false-positive. Resolving the parent still catches
        a pre-existing symlink *component* that redirects the write outside the target; P2
        (task #12) hardens the full TOCTOU story.
        """
        root = Path(os.path.realpath(target_root))
        parent = Path(os.path.realpath(path.parent))
        if parent != root and root not in parent.parents:
            raise FileOperationError(
                f"Refusing to write outside the target: {path} would be created under "
                f"{parent}, outside {root}. Choose a target inside the project directory.",
                file_path=str(path),
            )

    def _apply_action(self, action: Action, cfg: InstallConfig) -> None:
        """Perform one action's filesystem/subprocess effect. Raises on failure."""
        t = action.target
        if action.type is ActionType.MKDIR:
            t.mkdir(parents=True, exist_ok=True)
        elif action.type is ActionType.SYMLINK:
            if action.source is None:
                raise FileOperationError(
                    f"symlink action for {t} has no source", file_path=str(t)
                )
            t.parent.mkdir(parents=True, exist_ok=True)
            if t.is_symlink() or t.exists():
                t.unlink()
            os.symlink(action.source, t)
        elif action.type is ActionType.COPY_FILE:
            if action.source is None:
                raise FileOperationError(
                    f"copy action for {t} has no source", file_path=str(t)
                )
            t.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(action.source, t)
        elif action.type is ActionType.COPY_TREE:
            if action.source is None:
                raise FileOperationError(
                    f"copy-tree action for {t} has no source", file_path=str(t)
                )
            shutil.copytree(action.source, t, dirs_exist_ok=True)
        elif action.type is ActionType.WRITE_FILE:
            t.parent.mkdir(parents=True, exist_ok=True)
            t.write_text(action.content or "", encoding="utf-8")
            if action.mode is not None:
                os.chmod(t, action.mode)
        elif action.type is ActionType.CHMOD:
            if action.mode is not None:
                os.chmod(t, action.mode)
        elif action.type is ActionType.GITIGNORE_ENSURE:
            self._append_gitignore_line(t, action.detail or "")
        elif action.type is ActionType.RUN_SUBPROCESS:
            proc = self._run_subprocess(
                action.argv or [],
                cwd=action.cwd or t,
                timeout=SUBPROCESS_TIMEOUT_INSTALL,
            )
            if proc.returncode != 0:
                raise FileOperationError(
                    f"Command failed (exit {proc.returncode}): "
                    f"{' '.join(str(a) for a in action.argv or [])}. {proc.stderr.strip()}",
                    file_path=str(t),
                )
        elif action.type is ActionType.RECONCILE_COPY:
            self._reconcile_copy(cfg)
        else:  # pragma: no cover - defensive
            raise FileOperationError(
                f"Unknown action type {action.type!r}", file_path=str(t)
            )

    @staticmethod
    def _append_gitignore_line(gitignore: Path, line: str) -> None:
        """Append ``line`` to ``gitignore`` if absent, preserving a trailing newline (FR-10)."""
        existing = gitignore.read_text(encoding="utf-8") if gitignore.is_file() else ""
        if line in existing.splitlines():
            return
        prefix = "" if existing == "" or existing.endswith("\n") else "\n"
        gitignore.write_text(existing + prefix + line + "\n", encoding="utf-8")

    def _run_actions(self, actions: List[Action], cfg: InstallConfig) -> ExecuteResult:
        """Apply the planned action list with idempotent skip + rollback/repairable (FR-16).

        Writes a ``pending`` manifest first and a ``complete`` one last (R2-S7 crash marker):
        a process killed mid-run leaves ``pending`` so a later :meth:`execute` routes to repair.
        On a fresh-install failure the symlink path rolls back its own writes (including the
        pending manifest); the copy path is repairable-only (R2-S2), leaving ``pending``.
        """
        self._assert_embed_not_escaping(cfg)  # R2-S5 pre-flight
        effective = self._effective_method(cfg)
        embed = Path(cfg.target_root) / EMBED_DIR_NAME
        embed_preexisted = embed.is_symlink() or embed.exists()
        embed.mkdir(parents=True, exist_ok=True)
        managed = self._resolved_managed_paths(cfg.source_path, cfg.embed_profile)
        # Pending marker (R2-S7): written before any action so a crash is detectable.
        self.write_manifest(
            cfg.target_root,
            Manifest(
                method=effective,
                source_path=cfg.source_path,
                profiles=[p.lang for p in cfg.profiles],
                state=ManifestState.PENDING,
                embed_profile=cfg.embed_profile,
                managed_paths=managed,
            ),
        )
        has_subprocess = any(a.type is ActionType.RUN_SUBPROCESS for a in actions)
        applied: List[Action] = []
        # On a fresh install, rolling back removes the whole embed dir (and the pending
        # manifest); on a re-run we leave the pre-existing tree in place.
        newly_created: List[Path] = [] if embed_preexisted else [embed]
        owned_types = {
            ActionType.MKDIR,
            ActionType.SYMLINK,
            ActionType.WRITE_FILE,
            ActionType.COPY_FILE,
            ActionType.COPY_TREE,
        }
        try:
            for action in actions:
                self._confine(action.target, cfg.target_root)
                if action.is_satisfied():
                    self.logger.info(
                        "capdevpipe skip (already satisfied): %s", action.describe()
                    )
                    continue
                existed = action.target.is_symlink() or action.target.exists()
                self._apply_action(action, cfg)
                self._log_action(action)
                applied.append(action)
                if not existed and action.type in owned_types:
                    newly_created.append(action.target)
        except Exception as exc:  # noqa: BLE001 - rollback then report a clean result
            self.logger.error(
                "capdevpipe execute failed: %s",
                exc,
                extra={"pipeline_name": "capdevpipe_install"},
            )
            if has_subprocess:
                # External rsync writes cannot be cleanly reversed (R2-S2): repairable-only.
                return ExecuteResult(
                    success=False,
                    actions_applied=applied,
                    repairable=True,
                    error=str(exc),
                )
            self._rollback(newly_created)
            return ExecuteResult(
                success=False,
                actions_applied=applied,
                rolled_back=True,
                error=str(exc),
            )

        created_paths = [a.target for a in actions if a.type in owned_types]
        if effective is InstallMethod.COPY:
            # The rsync-produced files were not enumerated as actions; record the embed dir as
            # the copy-managed subtree so repair/drift/uninstall see them (R3-S2).
            created_paths.append(embed)
        manifest = Manifest(
            method=effective,
            source_path=cfg.source_path,
            created_paths=created_paths,
            profiles=[p.lang for p in cfg.profiles],
            state=ManifestState.COMPLETE,
            embed_profile=cfg.embed_profile,
            managed_paths=managed,
        )
        self.write_manifest(cfg.target_root, manifest)
        manifest_path = cfg.target_root / EMBED_DIR_NAME / MANIFEST_FILENAME
        return ExecuteResult(
            success=True, actions_applied=applied, manifest_path=manifest_path
        )

    def _rollback(self, newly_created: List[Path]) -> None:
        """Best-effort removal of paths this run created, newest first (FR-16)."""
        for path in reversed(newly_created):
            try:
                if path.is_symlink() or path.is_file():
                    path.unlink()
                elif path.is_dir():
                    shutil.rmtree(path)
            except OSError as exc:  # pragma: no cover - best effort
                self.logger.warning(
                    "capdevpipe rollback could not remove %s: %s", path, exc
                )

    # -- manifest (FR-18) -------------------------------------------------- #

    def _resolved_managed_paths(self, source: Path, profile: str) -> List[str]:
        """Top-level embed-dir entry names for *profile* — the canonical ``managed_paths`` set.

        Mirrors canonical ``ResolvedEmbedProfile.managed_paths()``: scripts + python aliases +
        packages + resource trees + copy_files, deduped and sorted. SDK-unique post-step
        artifacts (project-named wrapper, ``pipeline.env``, ``.gitignore``) are deliberately
        NOT included — they are tolerated extras under canonical verify, not managed paths
        (FR-A6, spike §0.2). Best-effort: on any resolution error, returns ``[]`` so the
        install still records a (canonical-shaped) manifest rather than crashing.
        """
        try:
            inv = resolve_embed_inventory(source, profile)
        except Exception as exc:  # noqa: BLE001 - manifest managed_paths are best-effort
            self.logger.warning("capdevpipe managed_paths resolution failed: %s", exc)
            return []
        names = [
            *inv.scripts,
            *inv.python_aliases,
            *inv.packages,
            *inv.resource_trees,
            *inv.copy_files,
        ]
        return sorted(set(names))

    def write_manifest(self, target: Path, manifest: Manifest) -> None:
        """Persist the install manifest to ``.cap-dev-pipe/.install-manifest.json`` (FR-18)."""
        embed_dir = target / EMBED_DIR_NAME
        embed_dir.mkdir(parents=True, exist_ok=True)
        path = embed_dir / MANIFEST_FILENAME
        path.write_text(json.dumps(manifest.to_dict(), indent=2), encoding="utf-8")
        self.logger.info(
            "capdevpipe manifest written: %s (%d paths, state=%s)",
            path,
            len(manifest.created_paths),
            manifest.state.value,
            extra={"pipeline_name": "capdevpipe_install"},
        )

    def read_manifest(self, target: Path) -> Optional[Manifest]:
        """Read the install manifest, or ``None`` if absent/unreadable (FR-18).

        Forward-compatible handling of an unknown ``manifest_version`` (graceful fallback)
        is added in P2 (R3-S5 / task #11); here a parse error degrades to ``None`` so callers
        re-derive from disk rather than crash.
        """
        path = target / EMBED_DIR_NAME / MANIFEST_FILENAME
        if not path.is_file():
            return None
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
            version = int(data.get("manifest_version", MANIFEST_VERSION))
            if version > MANIFEST_VERSION:
                # Forward-compat (R3-S5): a manifest written by a newer installer may have an
                # unknown shape — degrade to re-derive-from-disk rather than mis-parse.
                self.logger.warning(
                    "capdevpipe: manifest at %s has version %d > supported %d; "
                    "re-deriving from disk",
                    path,
                    version,
                    MANIFEST_VERSION,
                )
                return None
            return Manifest.from_dict(data)
        except (json.JSONDecodeError, KeyError, ValueError, TypeError) as exc:
            self.logger.warning(
                "capdevpipe: unreadable manifest at %s (%s); re-deriving from disk",
                path,
                exc,
            )
            return None

