class InstallMethod(str, Enum):
    """How the pipeline scripts / profile docs are embedded (FR-4)."""

    SYMLINK = "symlink"
    COPY = "copy"


class ReRunMode(str, Enum):
    """Idempotent re-run modes for an existing install (FR-12)."""

    RECONFIGURE = "reconfigure"  # re-prompt + rewrite config; leave embedded scripts
    UPGRADE = "upgrade"  # refresh script symlinks / re-rsync; prune orphans; keep config+profiles
    REPAIR = "repair"  # recreate only missing/broken symlinks, then verify
    REPLACE_PIPELINE_ENV = (
        "replace-pipeline.env"  # rewrite managed keys; preserve non-managed
    )
    DOCTOR = "doctor"  # source-relocation / health check (FR-17; P2)


class ActionType(str, Enum):
    """The kind of write a planned :class:`Action` performs."""

    MKDIR = "mkdir"
    SYMLINK = "symlink"
    COPY_FILE = "copy_file"
    COPY_TREE = "copy_tree"
    WRITE_FILE = "write_file"
    CHMOD = "chmod"
    GITIGNORE_ENSURE = "gitignore_ensure"
    RUN_SUBPROCESS = "run_subprocess"
    #: Apply-time copy-path reconcile (FR-6): merge the four managed pipeline.env keys into
    #: the rsync-written file and remove the auto-linked root java profile. Modeled as a
    #: single action because its effect depends on the rsync *output* (unknown at plan time).
    RECONCILE_COPY = "reconcile_copy"


class ManifestState(str, Enum):
    """In-progress marker carried by the manifest (FR-18; crash semantics in P2)."""

    PENDING = "pending"
    COMPLETE = "complete"


