    # -- profiles (FR-9) --------------------------------------------------- #

    @staticmethod
    def _is_review_artifact(path: Path) -> bool:
        """Exclude review artifacts from profile-doc detection (FR-9)."""
        if "arc-review" in (part.lower() for part in path.parts):
            return True
        name = path.name.lower()
        return name.startswith(("crp_", "crp-")) or "review" in name

    def detect_doc_candidates(self, target: Path) -> DocCandidates:
        """Detect plan/requirements docs in root and one-level ``docs/`` (FR-9).

        Matches ``*plan*.md`` / ``*requirements*.md`` case-insensitively, de-duplicated and
        deterministically ordered (by name then path), excluding review artifacts
        (``arc-review/``, ``CRP_*``, ``*REVIEW*``).
        """
        target = Path(target)
        plans: List[Path] = []
        reqs: List[Path] = []
        for directory in (target, target / "docs"):
            if not directory.is_dir():
                continue
            for doc in directory.glob("*.md"):
                if self._is_review_artifact(doc):
                    continue
                low = doc.name.lower()
                if "plan" in low:
                    plans.append(doc)
                if "requirement" in low:
                    reqs.append(doc)

        def _order(items: List[Path]) -> List[Path]:
            unique = {p.resolve(): p for p in items}
            return sorted(unique.values(), key=lambda p: (p.name.lower(), str(p)))

        return DocCandidates(plans=_order(plans), reqs=_order(reqs))

    def create_profile(self, cfg: InstallConfig, profile: ProfileSpec) -> List[Action]:
        """Action(s) to create ``<lang>/`` with ``<lang>-plan.md`` + ``<lang>-requirements.md``
        (FR-9). Profile doc links are **relative** (NFR-3) so a profile survives moving the
        project; the method (relative-symlink vs copy) comes from
        :meth:`_resolve_profile_method`."""
        embed = Path(cfg.target_root) / EMBED_DIR_NAME
        profile_dir = embed / profile.lang
        method = self._resolve_profile_method(cfg)
        actions: List[Action] = [Action(type=ActionType.MKDIR, target=profile_dir)]
        for doc, suffix in ((profile.plan, "plan"), (profile.reqs, "requirements")):
            if doc is None:
                continue
            link = profile_dir / f"{profile.lang}-{suffix}.md"
            doc_abs = Path(doc).resolve()
            if method is InstallMethod.SYMLINK:
                relative = Path(os.path.relpath(doc_abs, profile_dir.resolve()))
                actions.append(
                    Action(type=ActionType.SYMLINK, target=link, source=relative)
                )
            else:
                actions.append(
                    Action(type=ActionType.COPY_FILE, target=link, source=doc_abs)
                )
        return actions

    # -- gitignore / verify (FR-10, FR-11) --------------------------------- #

    def update_gitignore(self, cfg: InstallConfig) -> List[Action]:
        """Action to idempotently ensure ``.cap-dev-pipe/pipeline-output/`` is ignored
        (FR-10). Warns first if the target git repo has uncommitted changes (NFR-6)."""
        self._warn_if_repo_dirty(Path(cfg.target_root))
        gitignore = Path(cfg.target_root) / ".gitignore"
        return [
            Action(
                type=ActionType.GITIGNORE_ENSURE,
                target=gitignore,
                detail=f"{EMBED_DIR_NAME}/pipeline-output/",
            )
        ]

    def _warn_if_repo_dirty(self, target: Path) -> None:
        """Log a warning before mutating ``.gitignore`` in a dirty repo (NFR-6, best-effort)."""
        try:
            proc = subprocess.run(
                ["git", "status", "--porcelain"],
                cwd=str(target),
                capture_output=True,
                text=True,
                timeout=10,
            )
        except (OSError, subprocess.SubprocessError):
            return
        if proc.returncode == 0 and proc.stdout.strip():
            self.logger.warning(
                "capdevpipe: target git repo %s has uncommitted changes; .gitignore "
                "will be modified",
                target,
            )

    @staticmethod
    def _parse_listed_langs(stdout: str) -> List[str]:
        """Extract profile names from ``run.sh --list-langs`` output.

        The pipeline prints each profile as an indented ``<lang>/`` line under an
        "Available language profiles:" header, with ``plan:``/``reqs:`` detail lines
        beneath it. A profile name is therefore a stripped line that ends with ``/`` and
        contains no whitespace or ``:``; the header, the detail lines, and the
        no-profiles help text are all ignored. Returning bare names (not raw lines) lets
        :meth:`verify` test membership exactly instead of by substring — a substring test
        would, e.g., count ``go`` as present against a ``django/`` profile.
        """
        langs: List[str] = []
        for raw in stdout.splitlines():
            line = raw.strip()
            if not line.endswith("/"):
                continue
            name = line[:-1]
            if name and ":" not in name and not any(c.isspace() for c in name):
                langs.append(name)
        return langs

    def verify(self, target: Path) -> VerifyResult:
        """Verify the install by subprocess ``run.sh --list-langs`` (FR-11).

        Passes iff the subprocess exits 0, every created profile (per the manifest) appears
        in stdout, and the single-source property holds (local ``design/``+``prompts/`` and a
        resolvable ``run.sh`` in the embedded dir). A zero-profile "skip" install is a valid
        pass (R2-F7). A dangling embedded ``run.sh`` symlink is reported as a re-point
        diagnostic rather than a raw resolution error (basic FR-17; full doctor in P2).
        """
        embed = Path(target) / EMBED_DIR_NAME
        run_sh = embed / "run.sh"
        manifest = self.read_manifest(target)
        expected = manifest.profiles if manifest else []
        single_source_ok = (
            (embed / "design").is_dir()
            and (embed / "prompts").is_dir()
            and (run_sh.is_symlink() or run_sh.is_file())
        )
        if not (run_sh.is_symlink() or run_sh.is_file()):
            return VerifyResult(
                passed=False,
                expected_langs=expected,
                single_source_ok=False,
                message=f"Embedded run.sh not found at {run_sh}. Run install or repair.",
            )
        if run_sh.is_symlink() and not run_sh.exists():
            missing = Path(os.readlink(run_sh))
            return VerifyResult(
                passed=False,
                expected_langs=expected,
                single_source_ok=single_source_ok,
                dangling_source=missing,
                message=(
                    f"Embedded run.sh points at {missing}, which no longer exists — the "
                    f"cap-dev-pipe source likely moved. Re-point via `upgrade`."
                ),
            )
        proc = self._run_subprocess(
            [str(run_sh), "--list-langs"], cwd=embed, timeout=SUBPROCESS_TIMEOUT_VERIFY
        )
        listed = self._parse_listed_langs(proc.stdout)
        listed_set = set(listed)
        missing_profiles = [lang for lang in expected if lang not in listed_set]
        passed = proc.returncode == 0 and not missing_profiles and single_source_ok
        if passed:
            message = f"Verified: --list-langs ok; {len(expected)} profile(s) present."
        elif proc.returncode != 0:
            message = f"--list-langs exited {proc.returncode}: {proc.stderr.strip()}"
        elif missing_profiles:
            message = (
                f"Profiles missing from --list-langs: {', '.join(missing_profiles)}"
            )
        else:
            message = (
                "Single-source property failed: local design/prompts or run.sh missing."
            )
        return VerifyResult(
            passed=passed,
            listed_langs=listed,
            expected_langs=expected,
            single_source_ok=single_source_ok,
            exit_code=proc.returncode,
            stderr=proc.stderr,
            message=message,
        )

    # -- re-run (FR-12) ---------------------------------------------------- #

    def detect_existing(self, target: Path) -> InstallState:
        """Detect an existing install via the manifest + filesystem (FR-12)."""
        embed = Path(target) / EMBED_DIR_NAME
        if not (embed.exists() or embed.is_symlink()):
            return InstallState(exists=False)
        manifest = self.read_manifest(target)
        pending = manifest is not None and manifest.state is ManifestState.PENDING
        broken = [
            p for p in sorted(embed.glob("*")) if p.is_symlink() and not p.exists()
        ]
        return InstallState(
            exists=True, manifest=manifest, pending=pending, broken_symlinks=broken
        )

    def orphan_symlink_candidates(self, target: Path, cfg: InstallConfig) -> List[Path]:
        """Embed symlinks NOT in the current inventory — what ``upgrade`` would prune (read-only).

        Extracted so a ``--dry-run`` can preview the prune set without unlinking anything.
        """
        embed = Path(target) / EMBED_DIR_NAME
        inventory = resolve_embed_inventory(cfg.source_path, cfg.embed_profile)
        valid = inventory.symlink_top_level_names()
        return [
            entry
            for entry in (sorted(embed.iterdir()) if embed.is_dir() else [])
            if entry.is_symlink() and entry.name not in valid
        ]

    def _prune_orphan_symlinks(self, target: Path, cfg: InstallConfig) -> None:
        """Remove embed symlinks whose script was deleted upstream so embed == source (R2-F6)."""
        for entry in self.orphan_symlink_candidates(target, cfg):
            entry.unlink()
            self.logger.info("capdevpipe upgrade: pruned orphaned symlink %s", entry)

    def apply_mode(self, target: Path, mode: ReRunMode, cfg: InstallConfig) -> None:
        """Apply a re-run mode with a defined per-file change set (FR-12).

        - ``reconfigure``: rewrite config (pipeline.env, wrapper, profiles, gitignore);
          leave embedded scripts untouched.
        - ``upgrade``: refresh script embedding and prune symlinks orphaned by upstream
          deletions (R2-F6); keep config + profiles.
        - ``repair``: idempotently replay the full plan (only missing/incorrect entries
          change), then verify.
        - ``replace-pipeline.env``: rewrite only ``pipeline.env`` (managed keys; non-managed
          preserved, R3-F5).

        ``doctor`` (source-relocation, FR-17) is added in P2 (#16).
        """
        if mode is ReRunMode.REPAIR:
            self._run_actions(self.plan_actions(cfg), cfg)
            result = self.verify(target)
            if not result.passed:
                self.logger.warning(
                    "capdevpipe repair: verify failed — %s", result.message
                )
            return
        effective = self._effective_method(cfg)
        if mode is ReRunMode.RECONFIGURE:
            actions: List[Action] = []
            if effective is InstallMethod.SYMLINK:
                actions += self.write_pipeline_env(cfg)
            else:
                actions += self.reconcile_copy_install(cfg)
            actions += self.generate_wrapper(cfg)
            for profile in cfg.profiles:
                actions += self.create_profile(cfg, profile)
            actions += self.update_gitignore(cfg)
            self._run_actions(actions, cfg)
            return
        if mode is ReRunMode.UPGRADE:
            if effective is InstallMethod.SYMLINK:
                self._run_actions(self.embed_symlink(cfg), cfg)
            else:
                self._run_actions(
                    self.embed_copy(cfg) + self.reconcile_copy_install(cfg), cfg
                )
            self._prune_orphan_symlinks(target, cfg)
            return
        if mode is ReRunMode.REPLACE_PIPELINE_ENV:
            if effective is InstallMethod.SYMLINK:
                self._run_actions(self.write_pipeline_env(cfg), cfg)
            else:
                self._run_actions(self.reconcile_copy_install(cfg), cfg)
            return
        if mode is ReRunMode.DOCTOR:
            result = self.doctor(target)
            if not result.passed:
                self.logger.warning("capdevpipe doctor: %s", result.message)
            return
        raise NotImplementedError(
            f"apply_mode({mode.value}) is not a known re-run mode"
        )

    def doctor(self, target: Path) -> VerifyResult:
        """Health-check for a source-relocated symlink install (FR-17 / R2-S4).

        Detects a **dangling canonical source** — the absolute path the embedded script
        symlinks point at has moved/been deleted — and returns a re-point diagnostic naming
        the missing path rather than a raw resolution error. Falls through to :meth:`verify`
        when the source is intact.
        """
        embed = Path(target) / EMBED_DIR_NAME
        manifest = self.read_manifest(target)
        if manifest is not None and not Path(manifest.source_path).exists():
            return VerifyResult(
                passed=False,
                dangling_source=Path(manifest.source_path),
                message=(
                    f"Canonical source {manifest.source_path} no longer exists — it was "
                    f"moved or deleted. Re-point via `upgrade` with the new source path."
                ),
            )
        dangling = [
            p
            for p in (sorted(embed.glob("*")) if embed.is_dir() else [])
            if p.is_symlink() and not p.exists()
        ]
        if dangling:
            missing = Path(os.readlink(dangling[0]))
            return VerifyResult(
                passed=False,
                dangling_source=missing,
                message=(
                    f"Embedded symlink {dangling[0].name} points at {missing}, which is "
                    f"gone — the cap-dev-pipe source likely moved. Re-point via `upgrade`."
                ),
            )
        return self.verify(target)
