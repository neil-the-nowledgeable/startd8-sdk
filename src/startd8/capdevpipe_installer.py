    def embed_copy(self, cfg: InstallConfig) -> List[Action]:
        """Action to shell out to ``install-cap-dev-pipe.sh --force-pipeline-env <target>``
        (FR-6). The rsync writes scripts/design/prompts/pipeline.env into the target.

        Because the copy method *executes* a script from a (possibly overridden) source, it
        refuses an untrusted source unless the caller explicitly opted in via
        ``cfg.trust_source`` — the TUI sets this after surfacing/confirming the exact script
        path (R3-S1 / NFR-6 code-execution confinement)."""
        source = Path(cfg.source_path).resolve()
        installer_script = source / "install-cap-dev-pipe.sh"
        if not installer_script.is_file():
            raise ConfigurationError(
                f"Copy install requires {installer_script}, which is missing. "
                f"Point to a complete cap-dev-pipe checkout."
            )
        if not (cfg.trust_source or self._is_trusted_source(source)):
            raise ConfigurationError(
                f"Refusing to execute {installer_script} from an untrusted source. The copy "
                f"method runs this script; confirm the source is trusted (set trust_source=True, "
                f"or use the default {DEFAULT_SOURCE})."
            )
        embed = Path(cfg.target_root) / EMBED_DIR_NAME
        return [
            Action(
                type=ActionType.RUN_SUBPROCESS,
                target=embed,
                cwd=source,
                argv=[
                    str(installer_script),
                    "--embed-profile",
                    cfg.embed_profile,
                    "--force-pipeline-env",
                    str(cfg.target_root),
                ],
            )
        ]

