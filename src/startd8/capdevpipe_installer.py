    # -- pipeline.env helpers (shared by reconcile #4 and write_pipeline_env #5) -- #

    def _require_managed_keys(self, cfg: InstallConfig) -> Dict[str, str]:
        """Return the four managed values, or block (FR-7, R2-S6): never write a blank.

        Detection (env → ConfigManager → walk-up) fills ``cfg.pipeline_env`` upstream (#5);
        this is the consumption guard that refuses to proceed on any still-empty key.
        """
        env = cfg.pipeline_env or {}
        missing = [k for k in MANAGED_ENV_KEYS if not (env.get(k) or "").strip()]
        if missing:
            raise ConfigurationError(
                f"Cannot write pipeline.env: no value for {', '.join(missing)}. Provide "
                f"{'these keys' if len(missing) > 1 else 'this key'} explicitly — the "
                f"installer will not write a blank that fails later at --list-langs."
            )
        return {k: env[k].strip() for k in MANAGED_ENV_KEYS}

    def _merge_managed_env(self, existing: str, managed: Dict[str, str]) -> str:
        """Merge the managed keys into ``existing`` text, preserving everything else (R3-F5).

        Each managed key is replaced in place if present (first occurrence) else appended;
        comments, blank lines, and non-managed keys pass through unchanged.

        Format note (do NOT "fix" to ``export KEY=`` or add shell-escaping): cap-dev-pipe's
        ``pipeline/config.py:load_pipeline_env`` parses this file as plain ``key=value`` via
        ``line.partition("=")`` and ``key.strip()`` — it does NOT shell-source it. An
        ``export `` prefix would be captured *into the key* (``"export CONTEXTCORE_ROOT"``),
        making the value unreadable; ``$``/spaces in values are stored literally and need no
        escaping. The bare ``KEY="value"`` form here matches the canonical ``write_env_templates``
        output exactly. The ``(export\\s+)?`` in the match below only tolerates a pre-existing
        ``export`` line, rewriting it to the canonical bare form.
        """
        seen: set = set()
        out: List[str] = []
        for line in existing.splitlines():
            matched = next(
                (
                    k
                    for k in MANAGED_ENV_KEYS
                    if re.match(rf"^\s*(export\s+)?{re.escape(k)}=", line)
                ),
                None,
            )
            if matched is not None:
                if matched not in seen:
                    out.append(f'{matched}="{managed[matched]}"')
                    seen.add(matched)
                # drop duplicate managed lines
            else:
                out.append(line)
        for key in MANAGED_ENV_KEYS:
            if key not in seen:
                out.append(f'{key}="{managed[key]}"')
        return "\n".join(out) + "\n"

    def _reconcile_copy(self, cfg: InstallConfig) -> None:
        """Apply-time copy reconcile (FR-6): managed-key merge into pipeline.env + drop java."""
        embed = Path(cfg.target_root) / EMBED_DIR_NAME
        env_path = embed / "pipeline.env"
        managed = self._require_managed_keys(cfg)
        existing = env_path.read_text(encoding="utf-8") if env_path.is_file() else ""
        env_path.write_text(
            self._merge_managed_env(existing, managed), encoding="utf-8"
        )
        os.chmod(env_path, 0o600)  # NFR-6
        java = embed / "java"
        if java.is_symlink():
            java.unlink()
        elif java.is_dir():
            shutil.rmtree(java)
        if not (java.is_symlink() or java.exists()):
            self.logger.info(
                "capdevpipe reconcile: pipeline.env managed keys merged; "
                "auto-linked java profile cleared (TUI profile step is authoritative)"
            )

    # -- configuration (FR-7, FR-8) ---------------------------------------- #

    def detect_pipeline_env(self, cfg: InstallConfig) -> Dict[str, str]:
        """Detect the four managed keys: env var → ConfigManager → walk-up (FR-7).

        ``PROJECT_ROOT``/``PROJECT_NAME`` derive from the target; ``CONTEXTCORE_ROOT``/
        ``SDK_ROOT`` come from the env var, then a remembered ConfigManager preference, then
        a walk-up search of the target's ancestors for a sibling checkout. Any explicit,
        non-blank value already in ``cfg.pipeline_env`` (e.g. user-confirmed) wins. Keys that
        cannot be detected are left blank so the consumption guard blocks (R2-S6) — the TUI
        handler prompts the user to fill them before writing.
        """
        target = Path(cfg.target_root).resolve()
        detected: Dict[str, str] = {
            "PROJECT_ROOT": str(target),
            "PROJECT_NAME": target.name,
            "CONTEXTCORE_ROOT": self._detect_root(
                "CONTEXTCORE_ROOT", "ContextCore", PREF_CONTEXTCORE_ROOT, target
            ),
            "SDK_ROOT": self._detect_root(
                "SDK_ROOT", "startd8-sdk", PREF_SDK_ROOT, target
            ),
        }
        for key, value in (cfg.pipeline_env or {}).items():
            if (value or "").strip():
                detected[key] = value.strip()
        return detected

    @staticmethod
    def _detect_root(env_key: str, dir_name: str, pref_key: str, target: Path) -> str:
        """Resolve a root path: env var → ConfigManager preference → ancestor walk-up."""
        env_val = os.getenv(env_key)
        if env_val:
            return env_val
        try:
            pref = get_config_manager().get_preference(pref_key)
        except (
            Exception
        ):  # pragma: no cover - config unavailable; fall through to walk-up
            pref = None
        if pref:
            return str(pref)
        for ancestor in [target, *target.parents]:
            candidate = ancestor / dir_name
            if candidate.is_dir():
                return str(candidate)
        return ""

    def write_pipeline_env(self, cfg: InstallConfig) -> List[Action]:
        """Action to write ``pipeline.env`` with the four managed keys, mode ``0600``
        (FR-7, NFR-6). Blocks (raises :class:`ConfigurationError`) rather than writing a
        blank for any undetectable key (R2-S6); merges into any existing file so user-added
        non-managed keys survive a re-run (R3-F5); writes no secrets (NFR-4)."""
        managed = self._require_managed_keys(cfg)
        env_path = Path(cfg.target_root) / EMBED_DIR_NAME / "pipeline.env"
