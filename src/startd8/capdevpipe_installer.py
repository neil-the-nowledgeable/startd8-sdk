@dataclass
class Action:
    """One idempotent, replayable planned write (FR-13 preview == FR-16 execute set).

    Per R2-S3 every action is *create-or-skip-if-correct*: :meth:`is_satisfied` reports
    whether the on-disk state already matches, so ``repair``/``upgrade`` can replay the
    list filtered to missing/incorrect entries.
    """

    type: ActionType
    target: Path
    source: Optional[Path] = None
    content: Optional[str] = None
    mode: Optional[int] = None
    detail: Optional[str] = None
    argv: Optional[Sequence[str]] = None
    #: Working directory for a RUN_SUBPROCESS action (defaults to ``target`` if unset).
    cwd: Optional[Path] = None

    def describe(self) -> str:
        """Human-readable one-line description for the preview panel and logs."""
        if self.type is ActionType.SYMLINK:
            return f"symlink {self.target} -> {self.source}"
        if self.type in (ActionType.COPY_FILE, ActionType.COPY_TREE):
            return f"copy {self.source} -> {self.target}"
        if self.type is ActionType.WRITE_FILE:
            mode = f" (mode {oct(self.mode)})" if self.mode is not None else ""
            return f"write {self.target}{mode}"
        if self.type is ActionType.RUN_SUBPROCESS:
            return f"run {' '.join(self.argv or [])}"
        if self.type is ActionType.MKDIR:
            return f"mkdir {self.target}"
        if self.type is ActionType.CHMOD:
            return f"chmod {oct(self.mode) if self.mode is not None else '?'} {self.target}"
        if self.type is ActionType.GITIGNORE_ENSURE:
            return f"gitignore ensure {self.detail} in {self.target}"
        return f"{self.type.value} {self.target}"

    def is_satisfied(self) -> bool:
        """Whether on-disk state already matches this action (idempotency, R2-S3).

        ``True`` means :meth:`CapDevPipeInstaller.execute` skips the action on replay, so
        ``repair``/``upgrade`` re-run the list touching only missing/incorrect entries.
        ``RUN_SUBPROCESS`` is never "satisfied" (the external installer is itself idempotent,
        so re-running is safe and cheap).
        """
        t = self.target
        if self.type is ActionType.MKDIR:
            return t.is_dir()
        if self.type is ActionType.SYMLINK:
            # Satisfied iff it is a symlink already pointing at the intended (absolute) source.
            return t.is_symlink() and os.readlink(t) == str(self.source)
        if self.type is ActionType.WRITE_FILE:
            if not t.is_file() or t.is_symlink():
                return False
            if (
                self.content is not None
                and t.read_text(encoding="utf-8") != self.content
            ):
                return False
            if self.mode is not None and (t.stat().st_mode & 0o777) != self.mode:
                return False
            return True
        if self.type is ActionType.COPY_FILE:
            return t.is_file()
        if self.type is ActionType.COPY_TREE:
            return t.is_dir()
        if self.type is ActionType.CHMOD:
            return t.exists() and (t.stat().st_mode & 0o777) == (self.mode or 0)
        if self.type is ActionType.GITIGNORE_ENSURE:
            if not t.is_file() or self.detail is None:
                return False
            lines = t.read_text(encoding="utf-8").splitlines()
            return self.detail in lines
        # RUN_SUBPROCESS (and any unhandled type) is never pre-satisfied — always runs.
        return False


@dataclass
class Manifest:
    """Authoritative inventory persisted to ``.cap-dev-pipe/.install-manifest.json`` (FR-18)."""

    method: InstallMethod
    source_path: Path
    #: The full set of installer-*managed* paths (every owned-type planned target), NOT
    #: only paths this particular run physically created — a re-run/upgrade/repair records
    #: pre-existing and already-satisfied targets here too. This is the right inventory for
    #: repair/drift (the current consumers), which need the complete expected set. Run-scoped
    #: rollback uses a separate list (``_run_actions.newly_created``). NOTE for the P2
    #: uninstall (#?): do NOT blind-``rm`` this list — it includes ``pipeline.env`` (which may
    #: hold user-added non-managed keys) and any pre-existing content. Uninstall must diff
    #: against what it can prove it created, or confirm per path.
    created_paths: List[Path] = field(default_factory=list)
    profiles: List[str] = field(default_factory=list)
    state: ManifestState = ManifestState.COMPLETE
    manifest_version: int = MANIFEST_VERSION
    #: Embed profile and the canonical top-level managed-path set (FR-A6). These make the
    #: manifest readable by canonical ``verify_embed``/``repair_embed`` — which require
    #: ``embed_profile``/``install_method``/``managed_paths`` and otherwise crash with a raw
    #: ``KeyError`` on an SDK-written manifest (interop bug C1, spike-validated).
    embed_profile: str = DEFAULT_EMBED_PROFILE
    managed_paths: List[str] = field(default_factory=list)
    #: UTC ISO-8601 install timestamp (canonical field). ``None`` → stamped at ``to_dict`` time.
    installed_at: Optional[str] = None

    def to_dict(self) -> Dict[str, Any]:
        """Serialize for ``.install-manifest.json`` (FR-18 / FR-A6).

        Emits the **canonical** field set (so ``pipeline verify``/``repair`` accept the file)
        plus SDK-only bookkeeping (``created_paths``/``profiles``) that canonical
        ``read_install_manifest`` ignores as unknown keys. ``schema_version`` and
        ``manifest_version`` are both written at the same value for either reader.
        """
        return {
            "schema_version": self.manifest_version,
            "manifest_version": self.manifest_version,
            "install_method": self.method.value,
            "source_path": str(self.source_path),
            "embed_profile": self.embed_profile,
            "installed_at": self.installed_at
            or datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
            "managed_paths": sorted(set(self.managed_paths)),
            "state": self.state.value,
            # SDK-only bookkeeping (rollback / orphan-prune / uninstall). Canonical ignores these.
            "created_paths": [str(p) for p in self.created_paths],
            "profiles": list(self.profiles),
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "Manifest":
        """Reconstruct from parsed JSON (FR-18), tolerating legacy and canonical shapes (FR-A10).

        Reads ``install_method`` (canonical) falling back to legacy ``method``; ``schema_version``
        falling back to legacy ``manifest_version``. A pre-A6 SDK manifest (``method`` +
        ``created_paths``, no ``managed_paths``/``embed_profile``) still loads; the next write
        upgrades it to the canonical superset in place (migration-on-write, OQ-3).
        """
        method_raw = data.get("install_method", data.get("method"))
        return cls(
            method=InstallMethod(method_raw),
            source_path=Path(data["source_path"]),
            created_paths=[Path(p) for p in data.get("created_paths", [])],
            profiles=list(data.get("profiles", [])),
            state=ManifestState(data.get("state", ManifestState.COMPLETE.value)),
            manifest_version=int(
                data.get("schema_version", data.get("manifest_version", MANIFEST_VERSION))
            ),
            embed_profile=str(data.get("embed_profile", DEFAULT_EMBED_PROFILE)),
            managed_paths=list(data.get("managed_paths", [])),
            installed_at=data.get("installed_at"),
        )


