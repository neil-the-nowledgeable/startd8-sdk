"""Forward Manifest Validation against an extracted ManifestRegistry.

This module compares a prescribed `ForwardManifest` (Phase 3) against the actual
codebase represented by a `ManifestRegistry` (Phase 1). It computes structural
and semantic violations (`ContractViolation`), emitting warnings for advisory
breaches and errors for mandatory omissions (e.g. missing API endpoints or
function signatures).
"""

import logging
import re
from dataclasses import dataclass
from typing import Optional, List

from startd8.forward_manifest import ForwardManifest, InterfaceContract, ForwardFileSpec
from startd8.utils.manifest_registry import ManifestRegistry, _flatten_elements
from startd8.utils.code_manifest import ElementKind

logger = logging.getLogger(__name__)


@dataclass(frozen=True)
class ContractViolation:
    """A structural or semantic divergence between prescribed contracts and generated code."""
    contract_id: str
    violation_type: str
    expected: str
    actual: Optional[str] = None
    file_path: Optional[str] = None
    severity: str = "error"  # "error" for mandatory structures, "warning" for advisory formulas.


def validate_forward_manifest(
    manifest: ForwardManifest,
    registry: ManifestRegistry,
) -> List[ContractViolation]:
    """Validates generated code topology against front-loaded contracts.

    Args:
        manifest: The prescribed target topography of the project.
        registry: The actual source code topography generated by Artisan.

    Returns:
        List[ContractViolation]: List of anomalies detected, containing expected
        values, actual values, and severity mappings.
    """
    violations: List[ContractViolation] = []

    # 1. Validate File Specifications
    for file_path, spec in manifest.file_specs.items():
        violations.extend(_validate_file_spec(file_path, spec, registry))

    # 2. Validate Abstract Contracts
    for contract in manifest.contracts:
        violations.extend(_validate_contract(contract, registry))

    return violations


def _validate_file_spec(
    file_path: str,
    spec: ForwardFileSpec,
    registry: ManifestRegistry,
) -> List[ContractViolation]:
    """Verify that a specific file exists and contains prescribed elements."""
    violations: List[ContractViolation] = []
    
    file_manifest = registry.get(file_path)
    if not file_manifest:
        violations.append(
            ContractViolation(
                contract_id=f"file_exists:{file_path}",
                violation_type="missing_file",
                expected="File exists in project",
                file_path=file_path,
                severity="error",
            )
        )
        return violations

    elements = _flatten_elements(file_manifest.elements)

    # Validate elements
    if spec.elements:
        actual_names = {e.name for e in elements}
        for expected_el in spec.elements:
            if expected_el.name not in actual_names:
                violations.append(
                    ContractViolation(
                        contract_id=f"file_element:{file_path}:{expected_el.name}",
                        violation_type=f"missing_{expected_el.kind.value}",
                        expected=f"Element `{expected_el.name}` of kind `{expected_el.kind.value}`",
                        actual="Not found",
                        file_path=file_path,
                        severity="error",
                    )
                )

    # Validate required imports
    if spec.imports:
        actual_imports = [imp.module for imp in (file_manifest.imports or [])]
        for req_imp in spec.imports:
            found = any(req_imp.module in acc_imp for acc_imp in actual_imports)
            if not found:
                 violations.append(
                    ContractViolation(
                        contract_id=f"file_import:{file_path}:{req_imp.module}",
                        violation_type="missing_import",
                        expected=f"Module import `{req_imp.module}`",
                        actual="Not found",
                        file_path=file_path,
                        severity="error",
                    )
                )

    return violations


def _validate_contract(
    contract: InterfaceContract,
    registry: ManifestRegistry,
) -> List[ContractViolation]:
    """Routes specific contract categories to specific structural checkers."""
    violations: List[ContractViolation] = []

    if contract.category == "function_name":
        violations.extend(_validate_function_name(contract, registry))
    elif contract.category == "class_name":
        violations.extend(_validate_class_name(contract, registry))
    elif contract.category == "import_path":
        violations.extend(_validate_import_path(contract, registry))
    elif contract.category in ("formula", "render_pattern"):
        violations.extend(_validate_advisory(contract, registry))
    else:
        logger.debug(f"Validator skipped unsupported category: {contract.category}")

    return violations


def _validate_function_name(
    contract: InterfaceContract, registry: ManifestRegistry
) -> List[ContractViolation]:
    """Assert a function FQN exists and validates signature if provided."""
    violations: List[ContractViolation] = []
    
    if not contract.function_name:
        return violations

    # Determine standard FQN or perform broader regex extraction for methods
    fqn = contract.function_name
    match = registry.resolve_fqn(fqn)
    
    if match is None:
        violations.append(
            ContractViolation(
                contract_id=contract.contract_id,
                violation_type="missing_function",
                expected=f"Function `{fqn}` exists in project",
                severity="error",
            )
        )
        return violations
        
    file_path, element = match

    # Check if signature was strictly mandated via 'binding_text'
    if getattr(contract, "binding_text", "") and element.signature:
        expected_sig = contract.binding_text
        
        # Simple heuristic check if return signature provided in binding_text
        if "->" in expected_sig:
             expected_ret = expected_sig.split("->")[-1].strip()
             actual_ret = element.signature.return_annotation or "None"
             
             if expected_ret not in actual_ret:
                  violations.append(
                      ContractViolation(
                          contract_id=contract.contract_id,
                          violation_type="signature_mismatch",
                          expected=f"Return type `{expected_ret}`",
                          actual=f"Return type `{actual_ret}`",
                          file_path=file_path,
                          severity="error",
                      )
                  )

    return violations


def _validate_class_name(
    contract: InterfaceContract, registry: ManifestRegistry
) -> List[ContractViolation]:
    """Assert a class exists, and its structural base class hierarchy is correct."""
    violations: List[ContractViolation] = []
    if not contract.class_name:
        return violations
        
    fqn = contract.class_name
    match = registry.resolve_fqn(fqn)
    
    if match is None:
        violations.append(
            ContractViolation(
                contract_id=contract.contract_id,
                violation_type="missing_class",
                expected=f"Class `{fqn}` exists in project",
                severity="error",
            )
        )
        return violations
        
    file_path, element = match
    
    # Assert inheritance requirements if set
    if contract.base_class:
        expected_parent = contract.base_class
        actual_bases = element.bases or []
        
        if expected_parent not in actual_bases:
            violations.append(
                ContractViolation(
                    contract_id=contract.contract_id,
                    violation_type="missing_base_class",
                    expected=f"Class `{fqn}` inherits from `{expected_parent}`",
                    actual=f"Bases: {actual_bases}",
                    file_path=file_path,
                    severity="error",
                )
            )
            
    return violations


def _validate_import_path(
    contract: InterfaceContract, registry: ManifestRegistry
) -> List[ContractViolation]:
    """Scan all FileManifest structures asserting no forbidden files are missing."""
    violations: List[ContractViolation] = []
    
    if not contract.target_path:
        return violations
        
    req_lib = contract.target_path
    
    for file_path, file_manifest in registry._manifest_map.items():
        actual_imports = [imp.module for imp in (file_manifest.imports or [])]
        found = any(req_lib in acc_imp for acc_imp in actual_imports)
        
        # We need more context on *where* the import path is required. 
        # Generally, it's global to the project if not specified for a file.
        # But for pipeline validation, if this lib is not imported anywhere, the feature may be omitted.
    
    # Check if we searched all files and found nothing
    # Actually, import_path contracts are usually globally verified:
    global_imports = set()
    for file_manifest in registry._manifest_map.values():
        for imp in (file_manifest.imports or []):
             global_imports.add(imp.module)
             
    if not any(req_lib in acc_imp for acc_imp in global_imports):
         violations.append(
             ContractViolation(
                 contract_id=contract.contract_id,
                 violation_type="missing_dependency",
                 expected=f"Dependency `{req_lib}` is utilized in the codebase",
                 actual="Not found in any imports",
                 severity="error",
             )
         )

    return violations


def _validate_advisory(
    contract: InterfaceContract, registry: ManifestRegistry
) -> List[ContractViolation]:
    """Validate formulas and UI render templates. Treat failures as warnings."""
    # Since formulas are deeply semantic (e.g. "Calculate MRR where X > 0"),
    # we can't reliably structurally lint this without LLMs.
    # We yield a warning to alert agents that this contract could be breached.
    return [
         ContractViolation(
             contract_id=contract.contract_id,
             violation_type=f"unverified_{contract.category.value}",
             expected=f"Formula or Pattern `{contract.description}` verified",
             severity="warning",
             actual="Manual verification required",
         )
    ]
