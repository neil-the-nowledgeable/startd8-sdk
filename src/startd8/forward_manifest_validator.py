def _validate_requirements_coverage(
    requirements_content: str,
    sibling_imports: Dict[str, Set[str]],
    result: DiskComplianceResult,
) -> None:
    """Check that every package in requirements.in is imported (REQ-SV-601).

    Appends ``orphan_dependency`` warnings to *result.semantic_issues*
    for packages that no sibling Python file imports.
    """
    from startd8.implementation_engine.package_aliases import pypi_to_import

    all_imports: Set[str] = set()
    for imports in sibling_imports.values():
        all_imports |= imports

    for lineno, line in enumerate(requirements_content.splitlines(), 1):
        stripped = line.strip()
        if not stripped or stripped.startswith("#") or stripped.startswith("-"):
            continue

        # Extract package name (before version specifier)
        spec = stripped.split("#")[0].strip()
        package = re.split(r"[~=!<>\[;]", spec)[0].strip().lower()
        if not package or package in _KNOWN_NON_IMPORT_PACKAGES or package in _KNOWN_BUILD_TIME_PACKAGES:
            continue

        # Map package to expected import name
        expected_import = pypi_to_import(package)

        # Check if any sibling imports this package
        found = any(
            imp == expected_import
            or imp.startswith(expected_import + ".")
            or imp == package  # simple case: flask → flask
            or imp.startswith(package + ".")
            # REQ-SV2-300: reverse prefix — `from google.cloud import secretmanager`
            # produces imp="google.cloud", expected="google.cloud.secretmanager"
            or expected_import.startswith(imp + ".")
            for imp in all_imports
        )
        if not found:
            result.semantic_issues.append({
                "category": "orphan_dependency",
                "severity": "warning",
                "message": (
                    f"Package '{package}' in requirements.in is not "
                    f"imported by any sibling Python file"
                ),
                "line": lineno,
                "symbol": package,
            })


# Pure functions whose return value should not be discarded.
# Only includes functions detectable via AST callee extraction —
# i.e. the module/object name appears literally in the source code.
# Instance method calls like d.get() or s.replace() use the variable
# name, not the type name, so dict.get / str.replace are NOT included.
_PURE_FUNCTIONS = frozenset({
    "os.getenv",
    "os.environ.get",
    "os.path.join",
    "os.path.exists",
    "os.path.dirname",
    "os.path.basename",
    "os.path.abspath",
    "os.path.expanduser",
})


def _validate_discarded_returns(tree: ast.AST) -> List[Dict[str, object]]:
    """Flag expression statements that discard return values (REQ-SV-701).

    Only flags calls to functions in ``_PURE_FUNCTIONS`` — these are
    functions whose sole purpose is to return a value.  Side-effect
    calls like ``print()``, ``list.append()``, and ``logging.*`` are
    never flagged.
    """
    issues: List[Dict[str, object]] = []
    for node in ast.walk(tree):
        if not isinstance(node, ast.Expr):
            continue
        if not isinstance(node.value, ast.Call):
            continue

        callee = _extract_callee_name(node.value)
        if callee and callee in _PURE_FUNCTIONS:
            issues.append({
                "category": "discarded_return",
                "severity": "warning",
                "message": f"Return value of '{callee}' is discarded",
                "line": node.lineno,
                "symbol": callee,
            })
    return issues


def _extract_callee_name(call_node: ast.Call) -> Optional[str]:
    """Extract dotted name from a Call node (e.g. ``os.getenv``)."""
    func = call_node.func
    if isinstance(func, ast.Name):
        return func.id
    if isinstance(func, ast.Attribute):
        parts: List[str] = []
        node: Any = func
        while isinstance(node, ast.Attribute):
            parts.append(node.attr)
            node = node.value
        if isinstance(node, ast.Name):
            parts.append(node.id)
            return ".".join(reversed(parts))
    return None


def _validate_service_identity(
    tree: ast.AST,
    file_path: str,
    sibling_files: Optional[List[str]] = None,
) -> List[Dict[str, object]]:
    """Flag logger calls with a different service's name (REQ-SV2-400).

    Detects copy-paste bugs where ``logger.py`` is duplicated across
    services but the component/service name string is not updated.
    """
    issues: List[Dict[str, object]] = []
    parts = Path(file_path).parts
    if len(parts) < 2:
        return issues
    expected_service = parts[-2].lower()

    # Build set of other service directory names from siblings
    other_services: set[str] = set()
    for sib in sibling_files or []:
        sib_parts = Path(sib).parts
        if len(sib_parts) >= 2:
            other_services.add(sib_parts[-2].lower())
    other_services.discard(expected_service)
    if not other_services:
        return issues

    _LOGGER_CALLEES = {"getlogger", "getjsonlogger", "get_logger", "basicconfig"}
    _IDENTITY_KWARGS = {"component", "service", "name"}
    _IDENTITY_PARAMS = {"component", "service", "name"}

    def _check_string(value: str, lineno: int, context: str) -> None:
        found = value.lower()
        for other in other_services:
            if other in found and expected_service not in found:
                issues.append({
                    "category": "service_identity_mismatch",
                    "severity": "error",
                    "message": (
                        f"{context} contains '{other}' "
                        f"but file is in '{expected_service}/' directory"
                    ),
                    "line": lineno,
                    "symbol": value,
                })
                break

    for node in ast.walk(tree):
        # Check function/method default parameter values:
        # def getJSONLogger(name='emailservice') or
        # def __init__(self, component='emailservice')
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            func_lower = node.name.lower()
            # Only check logger-related functions or identity params
            for arg, default in zip(
                reversed(node.args.args), reversed(node.args.defaults)
            ):
                param_name = arg.arg.lower()
                if (
                    isinstance(default, ast.Constant)
                    and isinstance(default.value, str)
                    and (param_name in _IDENTITY_PARAMS or func_lower in _LOGGER_CALLEES)
                ):
                    _check_string(
                        default.value, node.lineno,
                        f"Default for '{arg.arg}' in '{node.name}()'",
                    )
            continue

        if not isinstance(node, ast.Call):
            continue
        callee = _extract_callee_name(node)
        callee_tail = (callee or "").lower().split(".")[-1] if callee else ""

        # Check positional arg on logger factory calls
        if callee_tail in _LOGGER_CALLEES and node.args:
            arg = node.args[0]
            if isinstance(arg, ast.Constant) and isinstance(arg.value, str):
                _check_string(
                    arg.value, node.lineno,
                    f"Logger initialized with '{arg.value}'",
                )

        # Check identity keyword args on any call
        for kw in node.keywords:
            if kw.arg not in _IDENTITY_KWARGS:
                continue
            if isinstance(kw.value, ast.Constant) and isinstance(kw.value.value, str):
                _check_string(
                    kw.value.value, node.lineno,
                    f"Keyword '{kw.arg}'",
                )

    return issues


def _validate_method_resolution(tree: ast.AST) -> List[Dict[str, object]]:
    """Flag self.x() where x is a module-level function, not a method (REQ-SV2-500).

    Catches the recurring ``self.index()`` vs ``index(self)`` pattern
    in Locust TaskSets and similar frameworks.
    """
    issues: List[Dict[str, object]] = []

    # Pass 1: module-level function names
    module_funcs: set[str] = set()
    for node in ast.iter_child_nodes(tree):
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            module_funcs.add(node.name)

    if not module_funcs:
        return issues

    # Pass 2: per-class check
    for node in ast.iter_child_nodes(tree):
        if not isinstance(node, ast.ClassDef):
            continue
        class_methods: set[str] = set()
        # Collect functions bound to the class via dict attributes (e.g.
        # Locust's `tasks = {index: 1, checkout: 2}` or @task decorators).
        # These are accessible via self.attr through framework dispatch.
        dispatch_funcs: set[str] = set()
        for item in ast.iter_child_nodes(node):
            if isinstance(item, (ast.FunctionDef, ast.AsyncFunctionDef)):
                class_methods.add(item.name)
            # Dict assignment: tasks = {func_name: weight, ...}
            if isinstance(item, ast.Assign):
                if isinstance(item.value, ast.Dict):
                    for key in item.value.keys:
                        if isinstance(key, ast.Name) and key.id in module_funcs:
                            dispatch_funcs.add(key.id)

        for child in ast.walk(node):
            if (
                isinstance(child, ast.Attribute)
                and isinstance(child.value, ast.Name)
                and child.value.id == "self"
                and child.attr in module_funcs
                and child.attr not in class_methods
                and child.attr not in dispatch_funcs
            ):
                issues.append({
                    "category": "method_resolution",
                    "severity": "warning",
                    "message": (
                        f"'self.{child.attr}()' called but '{child.attr}' "
                        f"is a module-level function, not a method of "
                        f"'{node.name}'"
                    ),
                    "line": child.lineno,
                    "symbol": child.attr,
                })

    return issues


# Framework decorator patterns that register functions externally
# (e.g., Flask/FastAPI routes, Click commands, pytest fixtures).
# Functions with these decorators are reachable via the framework's
# dispatch mechanism without explicit calls within the file.
_FRAMEWORK_DECORATOR_NAMES = frozenset({
    # Flask / FastAPI / Starlette
    "route", "get", "post", "put", "delete", "patch", "head", "options",
    "before_request", "after_request", "errorhandler", "teardown_appcontext",
    "before_serving", "after_serving",
    # Click / Typer
    "command", "group", "callback",
    # pytest
    "fixture", "mark",
    # Celery
    "task", "shared_task",
    # Django
    "receiver",
    # gRPC servicer registration
    "add_to_server",
})


def _has_framework_decorator(node: ast.FunctionDef) -> bool:
    """Return True if the function has a decorator that looks like a framework registration."""
    for dec in node.decorator_list:
        # @app.route(...), @router.get(...), @bp.errorhandler(...)
        if isinstance(dec, ast.Call) and isinstance(dec.func, ast.Attribute):
            if dec.func.attr in _FRAMEWORK_DECORATOR_NAMES:
                return True
        # @app.route (no call)
        if isinstance(dec, ast.Attribute):
            if dec.attr in _FRAMEWORK_DECORATOR_NAMES:
                return True
        # @pytest.fixture, @click.command
        if isinstance(dec, ast.Call) and isinstance(dec.func, ast.Attribute):
            if isinstance(dec.func.value, ast.Name):
                if dec.func.value.id in ("pytest", "click", "typer", "celery"):
                    return True
        # Bare @fixture, @task
        if isinstance(dec, ast.Name) and dec.id in _FRAMEWORK_DECORATOR_NAMES:
            return True
    return False


def _validate_reachability(tree: ast.AST) -> List[Dict[str, object]]:
    """Flag module-level functions never referenced within the file (REQ-SV2-600).

    Only flags public (non-underscore) functions that are not ``main``,
    not in ``__all__``, and not referenced by any ``ast.Name`` load or
    dict/list/set literal in the same module.
    """
    issues: List[Dict[str, object]] = []

    # Collect module-level function defs (skip private, main, class methods,
    # and functions with framework decorators that register them externally).
    func_defs: dict[str, int] = {}
    for node in ast.iter_child_nodes(tree):
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            if node.name.startswith("_") or node.name == "main":
                continue
            # Skip functions with framework decorators — these are reachable
            # via the framework's dispatch mechanism (e.g., Flask/FastAPI routes,
            # Click commands, pytest fixtures) without explicit call in the file.
            if _has_framework_decorator(node):
                continue
            func_defs[node.name] = node.lineno

    if not func_defs:
        return issues

    # Collect all Name-load references (function calls, dict values, etc.)
    referenced: set[str] = set()
    for node in ast.walk(tree):
        if isinstance(node, ast.Name) and isinstance(node.ctx, ast.Load):
            referenced.add(node.id)

    # Check __all__ exports
    for node in ast.iter_child_nodes(tree):
        if isinstance(node, ast.Assign):
            for target in node.targets:
                if isinstance(target, ast.Name) and target.id == "__all__":
                    if isinstance(node.value, (ast.List, ast.Tuple)):
                        for elt in node.value.elts:
                            if isinstance(elt, ast.Constant) and isinstance(elt.value, str):
                                referenced.add(elt.value)

    # A function defined at module level is "referenced" if its name
    # appears as a Name(Load) ANYWHERE in the file (including as a dict
    # key, decorator argument, list element, or function call).  The
    # definition itself uses Name(Store), so it won't false-match.
    for name, lineno in func_defs.items():
        if name not in referenced:
            issues.append({
                "category": "unreachable_function",
                "severity": "warning",
                "message": (
                    f"Module-level function '{name}' is defined "
                    f"but never called within the file"
                ),
                "line": lineno,
                "symbol": name,
            })

    return issues


def _emit_semantic_observability(
    result: "DiskComplianceResult",
    file_path: str,
    import_map: Optional[Dict[str, str]],
) -> None:
    """Log semantic issues and set OTel span attributes (REQ-SV-901/902)."""
    # Loki logging (REQ-SV-902)
    for issue in result.semantic_issues:
        if isinstance(issue, dict):
            logger.warning(
                "Semantic issue: %s",
                issue.get("message", str(issue)),
                extra={
                    "category": issue.get("category", "unknown"),
                    "severity": issue.get("severity", "unknown"),
                    "file_path": file_path,
                    "line": issue.get("line"),
                },
            )

    # OTel span attributes (REQ-SV-901)
    try:
        from opentelemetry import trace

        span = trace.get_current_span()
        if span.is_recording():
            error_count = 0
            warning_count = 0
            categories: Set[str] = set()
            for i in result.semantic_issues:
                if isinstance(i, dict):
                    sev = i.get("severity")
                    if sev == "error":
                        error_count += 1
                    elif sev == "warning":
                        warning_count += 1
                    cat = i.get("category")
                    if cat:
                        categories.add(cat)
            span.set_attribute(
                "semantic_validation.issues_count",
                len(result.semantic_issues),
            )
            span.set_attribute("semantic_validation.error_count", error_count)
            span.set_attribute("semantic_validation.warning_count", warning_count)
            if categories:
                span.set_attribute(
                    "semantic_validation.categories", sorted(categories),
                )
            span.set_attribute(
                "semantic_validation.import_map_mode",
                import_map is not None,
            )
    except Exception as exc:
        logger.debug("OTel span attributes skipped: %s", exc)


def _validate_import_resolution(
    tree: ast.AST,
    file_path: str,
    project_root: str,
    *,
    sibling_files: Optional[List[str]] = None,
    import_map: Optional[Dict[str, str]] = None,
) -> List[Dict[str, object]]:
    """Validate that all imports resolve to known sources (REQ-SV-201).

    Returns a list of structured semantic-issue dicts for unresolvable
    imports.  Each dict has ``category``, ``severity``, ``message``,
    ``line``, and ``symbol`` keys.
    """
    from startd8.utils.import_resolution import (
        extract_import_modules,
        discover_sibling_modules,
        resolve_import,
        parse_requirements_packages,
    )

    issues: List[Dict[str, object]] = []

    # Build sibling module set
    if sibling_files is not None:
        sibling_modules: Set[str] = set()
        for f in sibling_files:
            p = Path(f)
            sibling_modules.add(p.stem)
            if p.parent.name:
                sibling_modules.add(p.parent.name)
    else:
        sibling_modules = discover_sibling_modules(file_path, project_root)

    # Build requirements package set from sibling requirements.in
    requirements_packages = _discover_requirements_packages(
        file_path, project_root,
    )

    # Build set of proto stubs that actually exist as sibling files.
    # When sibling_files is available, _pb2 imports are verified against it.
    # This catches hallucinated proto names like email_service_pb2 when the
    # actual proto stub is demo_pb2.  (Run-055 F-2)
    known_proto_stems: Optional[Set[str]] = None
    if sibling_files is not None:
        known_proto_stems = set()
        from startd8.utils.requirements_generator import _PROTOBUF_STUB_RE
        for f in sibling_files:
            stem = Path(f).stem
            if _PROTOBUF_STUB_RE.match(stem):
                known_proto_stems.add(stem)

    # Resolve each import — try full dotted path first (catches google.api_core
    # via alias prefix matching), then fall back to top-level module name.
    for imp in extract_import_modules(tree):
        module_name = str(imp["module"])
        full_path = str(imp["full_path"])
        line = imp["line"]

        resolution = resolve_import(
            full_path,
            sibling_modules=sibling_modules,
            requirements_packages=requirements_packages,
            import_map=import_map,
        )
        if resolution is None and full_path != module_name:
            resolution = resolve_import(
                module_name,
                sibling_modules=sibling_modules,
                requirements_packages=requirements_packages,
                import_map=import_map,
            )

        # Proto stub verification: if the import resolved as "proto" but
        # we have a sibling file inventory and no matching _pb2.py exists,
        # downgrade to error.  (Run-055 F-2: email_service_pb2 accepted
        # by regex but actual proto is demo_pb2.)
        if (
            resolution is not None
            and resolution == "proto"
            and known_proto_stems is not None
            and module_name not in known_proto_stems
        ):
            issues.append({
                "category": "import_resolution",
                "severity": "error",
                "message": (
                    f"Proto stub '{module_name}' not found among sibling files. "
                    f"Known proto stubs: {sorted(known_proto_stems) or '(none)'}"
                ),
                "line": line,
                "symbol": full_path,
            })
            continue

        if resolution is None:
            issues.append({
                "category": "import_resolution",
                "severity": "error",
                "message": (
                    f"Unresolvable import: '{full_path}' is not stdlib, "
                    f"not in requirements.in, not a local module, "
                    f"and not a protobuf stub"
                ),
                "line": line,
                "symbol": full_path,
            })

    return issues


def _discover_requirements_packages(
    file_path: str, project_root: str,
) -> Set[str]:
    """Discover the project's declared dependency surface (FR-RI-1a, RUN-038 #1).

    Walks UP from the generated file to ``project_root`` (generated files live under e.g.
    ``generated/app/`` while the dependency manifest sits at the project root), and reads every
    common Python dependency manifest — ``requirements.in``/``.txt``, ``requirements-*.txt``, and
    ``pyproject.toml`` ``[project.dependencies]`` — not just ``requirements.in`` in the immediate
    parent. Returns the union of declared top-level package names.
    """
    from startd8.utils.import_resolution import parse_requirements_packages

    root = Path(project_root)
    abs_path = root / file_path
    packages: Set[str] = set()

    # Directories from the file's parent up to (and including) the project root.
    search_dirs: List[Path] = []
    cur = abs_path.parent
    while True:
        search_dirs.append(cur)
        if cur == root or root not in cur.parents:
            break
        cur = cur.parent
    if root not in search_dirs:
        search_dirs.append(root)

    seen: Set[Path] = set()
    for d in search_dirs:
        if d in seen:
            continue
        seen.add(d)
        # requirements*.txt / requirements.in (requirements format)
        try:
            for req_file in sorted(d.glob("requirements*.txt")) + sorted(d.glob("requirements*.in")):
                if req_file.is_file():
                    try:
                        packages |= parse_requirements_packages(
                            req_file.read_text(encoding="utf-8", errors="replace")
                        )
                    except OSError:
                        pass
        except OSError:
            pass
        # pyproject.toml [project.dependencies]
        pyproject = d / "pyproject.toml"
        if pyproject.is_file():
            try:
                packages |= _parse_pyproject_dependencies(
                    pyproject.read_text(encoding="utf-8", errors="replace")
                )
            except OSError:
                pass
    return packages


def _parse_pyproject_dependencies(content: str) -> Set[str]:
    """Extract top-level package names from a pyproject.toml ``[project.dependencies]`` array.

    Best-effort + dependency-light: tries ``tomllib`` (3.11+), falls back to the existing
    requirements parser over the dependency string lines. Never raises.
    """
    from startd8.utils.import_resolution import parse_requirements_packages

    try:
        import tomllib  # py3.11+

        data = tomllib.loads(content)
        deps = (data.get("project", {}) or {}).get("dependencies", []) or []
        return parse_requirements_packages("\n".join(str(d) for d in deps))
    except Exception:
        # Fallback: scan a `dependencies = [ ... ]` block textually.
        import re

        m = re.search(r"dependencies\s*=\s*\[(.*?)\]", content, re.DOTALL)
        if not m:
            return set()
        lines = re.findall(r"['\"]([^'\"]+)['\"]", m.group(1))
        return parse_requirements_packages("\n".join(lines))


def _extract_import_modules(tree: ast.AST) -> List[str]:
    """Extract all imported module names from an AST."""
    modules: List[str] = []
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            for alias in node.names:
                modules.append(alias.name)
        elif isinstance(node, ast.ImportFrom):
            if node.module:
                modules.append(node.module)
    return modules


def _validate_disk_file_against_spec(
    tree: ast.AST,
    spec: ForwardFileSpec,
    file_path: str,
) -> List[ContractViolation]:
    """Check disk AST against forward manifest spec (simplified, no registry)."""
    violations: List[ContractViolation] = []

    # Check elements exist
    if spec.elements:
        actual_names: set = set()
        for node in ast.walk(tree):
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
                actual_names.add(node.name)
        for expected_el in spec.elements:
            if expected_el.name not in actual_names:
                violations.append(ContractViolation(
                    contract_id=f"disk:{file_path}:{expected_el.name}",
                    violation_type=f"missing_{expected_el.kind.value}",
                    expected=f"Element `{expected_el.name}`",
                    actual="Not found on disk",
                    file_path=file_path,
                    severity="error",
                ))

    # Check imports
    if spec.imports:
        actual_imports = _extract_import_modules(tree)
        for req_imp in spec.imports:
            found = any(
                ai == req_imp.module or ai.startswith(req_imp.module + ".")
                for ai in actual_imports
            )
            if not found:
                violations.append(ContractViolation(
                    contract_id=f"disk_import:{file_path}:{req_imp.module}",
                    violation_type="missing_import",
                    expected=f"Import `{req_imp.module}`",
                    actual="Not found on disk",
                    file_path=file_path,
                    severity="error",
                ))

    return violations
