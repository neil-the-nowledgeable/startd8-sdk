@dataclass
class DiskComplianceResult:
    """Post-assembly disk validation result for a single file."""

    file_path: str
    ast_valid: bool = True
    stubs_remaining: int = 0
    duplicate_definitions: int = 0
    import_completeness: float = 1.0
    contract_compliance: float = 1.0
    contract_violations: List[ContractViolation] = field(default_factory=list)
    semantic_issues: List[Any] = field(default_factory=list)
    convention_violations: List[Any] = field(default_factory=list)  # FR-CAR-7: house-style (convention) gate
    error: Optional[str] = None


def _is_functionally_empty_python_module(tree: ast.AST) -> bool:
    """True if a module defines NO functions/classes and every top-level statement is
    boilerplate (imports, docstring, ``__all__``, ``pass``) or a None-valued assignment
    (``x = None`` / ``x: T = None`` / ``x: T``). This is the ``router = None``
    under-generation stub (RUN-036): it declares names but implements nothing. A module
    with any real def, or any assignment to a non-None value, is NOT functionally empty.
    """
    for stmt in getattr(tree, "body", []):
        if isinstance(stmt, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
            return False
        if isinstance(stmt, (ast.Import, ast.ImportFrom, ast.Pass)):
            continue
        if isinstance(stmt, ast.Expr) and isinstance(stmt.value, ast.Constant):
            continue  # docstring / bare literal
        if isinstance(stmt, ast.AnnAssign):
            if stmt.value is None or (
                isinstance(stmt.value, ast.Constant) and stmt.value.value is None
            ):
                continue
            return False
        if isinstance(stmt, ast.Assign):
            names = {t.id for t in stmt.targets if isinstance(t, ast.Name)}
            if names == {"__all__"}:
                continue  # export list is boilerplate
            if isinstance(stmt.value, ast.Constant) and stmt.value.value is None:
                continue  # x = None  ← the stub signal
            return False
        return False  # any other top-level statement (if/for/with/call/...) is substantive
    return True


def _lookup_file_spec(manifest: "ForwardManifest", file_path: str):
    """Find the manifest spec for *file_path* (exact, then path-suffix match). None if absent."""
    specs = getattr(manifest, "file_specs", None) or {}
    if file_path in specs:
        return specs[file_path]
    for key, spec in specs.items():
        if key.endswith("/" + file_path) or file_path.endswith("/" + key):
            return spec
    return None


def validate_disk_compliance(
    file_path: str,
    project_root: str,
    manifest: Optional[ForwardManifest] = None,
    *,
    sibling_files: Optional[List[str]] = None,
    sibling_imports: Optional[Dict[str, Set[str]]] = None,
    import_map: Optional[Dict[str, str]] = None,
    factory_patterns: Optional[List[str]] = None,
) -> DiskComplianceResult:
    """Validate the file on disk against forward manifest after assembly.

    Produces a structured compliance result that feeds post-mortem scoring.
    Only processes Python files.

    Args:
        file_path: Relative or absolute path to the file.
        project_root: Project root directory.
        manifest: Optional forward manifest for contract compliance.
        sibling_files: Optional list of sibling file paths for import
            resolution.  When absent the validator scans the parent
            directory on disk.
        sibling_imports: Optional mapping of sibling file path to import
            sets.  Used by L5 requirements cross-check to verify that
            every package in ``requirements.in`` is actually imported.
        import_map: Optional golden-seed import map (REQ-GS-302).
            When provided, import resolution operates in closed-world
            mode — any import not in the map is an error.
        factory_patterns: Optional list of regex patterns for factory
            function names (L4).  Defaults to ``create_*``, ``make_*``,
            ``build_*``, ``*_factory``.

    Returns:
        DiskComplianceResult with stubs, duplicates, import completeness, etc.
    """
    result = DiskComplianceResult(file_path=file_path)

    abs_path = Path(project_root) / file_path
    if not abs_path.is_file():
        result.ast_valid = False
        result.error = "file_not_found"
        return result

    # Non-Python file validators (KZ-Q4)
    if abs_path.suffix != ".py":
        return _validate_non_python_file(
            abs_path, result, sibling_imports=sibling_imports,
        )

    try:
        source = abs_path.read_text(encoding="utf-8")
    except OSError as exc:
        result.ast_valid = False
        result.error = f"read_error: {exc}"
        return result

    # AST validity
    try:
        tree = ast.parse(source)
    except SyntaxError as exc:
        result.ast_valid = False
        result.error = f"syntax_error: {exc}"
        return result

    # RUN-036 under-generation guard: a module the manifest declares FILLABLE but that
    # generates only None-assignments / imports / __all__ (e.g. `job_export_router = None`)
    # is a $0 stub — it otherwise scores 1.0 (no code → no stubs, no convention violations,
    # nothing to penalize), a measurement-integrity hole that makes "fixed" look fixed when
    # the feature was avoided by generating nothing. Mirror the non-Python empty-stem guard:
    # mark it non-valid so it cannot score perfect. Conservative — fires ONLY when the
    # manifest prescribes fillable elements for THIS file (no manifest / non-fillable spec /
    # re-export module → no guard, so legitimate __init__.py re-exports are unaffected).
    if manifest is not None and _is_functionally_empty_python_module(tree):
        _spec = _lookup_file_spec(manifest, file_path)
        _elems = getattr(_spec, "elements", None) if _spec is not None else None
        if _elems:
            try:
                from startd8.element_fillability import is_fillable_spec

                _fillable = is_fillable_spec(_elems)
            except (ImportError, TypeError):
                _fillable = False
            if _fillable:
                result.ast_valid = False
                result.contract_compliance = 0.0
                result.error = "under_generation: functionally_empty_module"
                result.semantic_issues.append({
                    "category": "under_generated_module",
                    "severity": "error",
                    "message": (
                        "manifest prescribes fillable elements but the generated module is "
                        "functionally empty (only None-assignments / imports / __all__) — "
                        "a $0 stub, not an implementation"
                    ),
                    "file": file_path,
                })
                return result

    # Count stubs: raise NotImplementedError / bare pass in function/method bodies
    result.stubs_remaining = _count_stubs(tree)

    # Count duplicate definitions at module level
    result.duplicate_definitions = _count_duplicate_definitions(tree)

    # House-style (convention) detection — FR-CAR-7 / FR-CAR-3. Advisory record here; the hard-gate
    # lives in compute_disk_quality_score. Kept DISTINCT from semantic_issues (no double-count, R1-F4):
    # the convention detectors (framework/orm/module-source/template) don't overlap the AST semantic checks.
    try:
        from .repair.convention import detect_conventions

        result.convention_violations = detect_conventions(source, file=file_path)
    except Exception:  # detection must never break disk validation
        result.convention_violations = []

    # --- Semantic validation (REQ-SV-200) ---
    # Only runs when the AST parsed successfully.

    # L1: Import resolution (REQ-SV-201)
    import_issues = _validate_import_resolution(
        tree, file_path, project_root,
        sibling_files=sibling_files,
        import_map=import_map,
    )
    result.semantic_issues.extend(import_issues)

    # L2: Cross-scope duplicate detection (REQ-SV-301)
    scope_dupe_issues = _detect_cross_scope_duplicates(tree)
    result.semantic_issues.extend(scope_dupe_issues)

    # L4: Factory return value check (REQ-SV-501)
    factory_issues = _validate_factory_returns(tree, patterns=factory_patterns)
    result.semantic_issues.extend(factory_issues)

    # L6: Discarded return value detection (REQ-SV-701)
    discarded_issues = _validate_discarded_returns(tree)
    result.semantic_issues.extend(discarded_issues)

    # L8: Service identity mismatch (REQ-SV2-400)
    identity_issues = _validate_service_identity(tree, file_path, sibling_files)
    result.semantic_issues.extend(identity_issues)

    # L9: Method resolution — self.x() where x is module-level, not a method (REQ-SV2-500)
    method_issues = _validate_method_resolution(tree)
    result.semantic_issues.extend(method_issues)

    # L10: Dead code — module-level functions never called within the file (REQ-SV2-600)
    reachability_issues = _validate_reachability(tree)
    result.semantic_issues.extend(reachability_issues)

    # L11: Python AST semantic checks — duplicate main guards, bare except:pass
    # (REQ-KZ-001: semantic dispatch completeness)
    try:
        from startd8.validators.semantic_checks import (
            check_duplicate_main_guards,
            check_bare_except_pass,
            check_fake_work_stub,
        )

        for _check_fn in (
            check_duplicate_main_guards,
            check_bare_except_pass,
            check_fake_work_stub,
        ):
            for issue in _check_fn(tree):
                result.semantic_issues.append({
                    "category": issue.check,
                    "severity": issue.severity,
                    "message": str(issue.message)[:200],
                    "line": issue.line,
                })
    except ImportError:
        pass

    # L12: Symbol-level import resolution (F-6.1 — RUN-009/010 phantom-import
    # class).  L1 resolves the *module*; this resolves the *symbol* — `from
    # starlette.responses import TemplateResponse` passes L1 (module exists)
    # but can never import.  Static AST of the resolved module; no execution.
    # L13: Referenced-asset contracts (F-6.2) — templates named in
    # TemplateResponse/get_template calls must exist on disk post-merge.
    try:
        from startd8.validators.import_symbol_checks import (
            check_import_symbols,
            check_template_references,
        )

        result.semantic_issues.extend(
            check_import_symbols(tree, file_path, project_root)
        )
        result.semantic_issues.extend(
            check_template_references(tree, file_path, project_root)
        )
    except Exception:  # the gate must never break disk validation
        pass

    # L14: Provenance-vocabulary validation (F-7 — RUN-010 D2). String
    # literals written to contract-owned enum/provenance fields must come
    # from the Prisma schema's declared domains (enums + provenance
    # @default values); invented vocabulary (`ownerId='default_owner'`,
    # `source='wizard'`) makes rows silently invisible to every filter.
    # No-op when no .prisma contract is discoverable.
    try:
        from startd8.validators.contract_vocabulary import (
            check_contract_vocabulary,
            discover_prisma_schema,
        )

        _prisma_schema = discover_prisma_schema(file_path, project_root)
        if _prisma_schema is not None and _prisma_schema.models:
            result.semantic_issues.extend(
                check_contract_vocabulary(
                    tree, _prisma_schema, file_path=file_path,
                )
            )
    except Exception:  # the gate must never break disk validation
        pass

    # --- Semantic issue logging and OTel (REQ-SV-901/902) ---
    _emit_semantic_observability(result, file_path, import_map)

    # Contract compliance via manifest
    if manifest and file_path in manifest.file_specs:
        spec = manifest.file_specs[file_path]
        violations = _validate_disk_file_against_spec(tree, spec, file_path)
        result.contract_violations = violations

        total_checks = len(spec.elements or []) + len(spec.imports or [])
        if total_checks > 0:
            error_violations = sum(1 for v in violations if v.severity == "error")
            result.contract_compliance = max(
                0.0, 1.0 - (error_violations / total_checks)
            )

        # Import completeness
        if spec.imports:
            actual_imports = _extract_import_modules(tree)
            matched = sum(
                1 for req_imp in spec.imports
                if any(
                    ai == req_imp.module or ai.startswith(req_imp.module + ".")
                    for ai in actual_imports
                )
            )
            result.import_completeness = matched / len(spec.imports)

    return result


