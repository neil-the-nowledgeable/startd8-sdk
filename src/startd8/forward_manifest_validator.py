# Language-specific severity weights for semantic penalty calculation (P3-2).
# Go errors are more critical (compiler enforces); Python/Node.js are more lenient.
_LANGUAGE_SEVERITY_WEIGHTS: Dict[str, Dict[str, float]] = {
    "go": {"error": 0.4, "warning": 0.15},
    "java": {"error": 0.35, "warning": 0.1},
    "csharp": {"error": 0.35, "warning": 0.1},
    "nodejs": {"error": 0.3, "warning": 0.1},
    "python": {"error": 0.3, "warning": 0.1},
}
_DEFAULT_SEVERITY_WEIGHTS = {"error": 0.3, "warning": 0.1}


def _compute_semantic_penalty(
    semantic_issues: list,
    language_id: Optional[str] = None,
) -> float:
    """Compute severity-weighted semantic penalty from issues list.

    Args:
        semantic_issues: List of semantic issue dicts with 'severity' key.
        language_id: Optional language ID for language-specific weights.
            Falls back to Python weights (backward compatible).

    Returns:
        Float in [0.0, 1.0] where 1.0 means no semantic issues.
    """
    weights = _LANGUAGE_SEVERITY_WEIGHTS.get(
        language_id or "python", _DEFAULT_SEVERITY_WEIGHTS,
    )
    error_count = 0
    warning_count = 0
    for issue in (semantic_issues or []):
        sev = issue.get("severity", "warning") if isinstance(issue, dict) else "warning"
        if sev == "error":
            error_count += 1
        else:
            warning_count += 1
    return max(0.0, 1.0 - error_count * weights["error"] - warning_count * weights["warning"])


def _convention_gating_enabled() -> bool:
    """FR-CAR-11: is the error-severity convention hard-gate active?

    Default ON — the advisory→gating flip's numeric precondition is satisfied (measured FP 0% over the
    governed in-architecture corpus, < the X=5% threshold; see ``compute_disk_quality_score``). Set
    ``STARTD8_CONVENTION_GATING=0`` (or ``false``/``no``/``off``) to revert to advisory: convention
    violations are still detected and recorded on the compliance result, but do not hard-zero the score.
    """
    return os.getenv("STARTD8_CONVENTION_GATING", "1").strip().lower() in ("1", "true", "yes", "on")


def compute_disk_quality_score(
    compliance: Any,
    language_id: Optional[str] = None,
) -> float:
    """Compute a composite disk quality score from DiskComplianceResult.

    Formula:
        composite = (contract_compliance × 0.4) + (import_completeness × 0.2)
                  + (stub_penalty × 0.2) + (semantic_penalty × 0.2)

    Args:
        compliance: DiskComplianceResult instance or SimpleNamespace with same fields.
        language_id: Optional language ID for language-specific severity weights.
            Falls back to Python weights (backward compatible).

    Returns:
        Float score in [0.0, 1.0].
    """
    if compliance is None:
        return 0.0
    if not getattr(compliance, "ast_valid", True):
        return 0.0
    # FR-CAR-7 hard-gate (R1-F8): an error-severity house-style (convention) violation fails the file
    # outright — additive, exactly like the ast_valid gate — so a structurally-clean-but-semantically-wrong
    # file (e.g. a lint-clean Flask view) scores 0.0 instead of ~0.8. Chosen over a weighted 5th term, which
    # would re-normalize the 1.0-sum formula and destabilize every existing threshold + the corpus calibration.
    #
    # FR-CAR-11 (advisory→gating ramp): the gate fires only when STARTD8_CONVENTION_GATING enables it.
    # Default is ON because the Exit-A→Phase-B precondition is now MET — the detector's measured
    # false-positive rate is 0% over N=19 governed in-architecture files (the deployed strtd8 app/, owned
    # FastAPI/SQLModel kinds) plus the lock-step parity corpus, well under the X=5% threshold. (The single
    # detector hit on app/ai/extract.py is the FR-CAR-4-carved bespoke dual-pattern file, not a gating FP.)
    # Set STARTD8_CONVENTION_GATING=0 to revert to advisory (violations still recorded, no hard-zero) —
    # the off-switch §4 mandates for projects/architectures where the FP rate is not yet measured.
    if _convention_gating_enabled() and any(
        getattr(c, "severity", "error") == "error"
        for c in getattr(compliance, "convention_violations", []) or []
    ):
        return 0.0

    contract_compliance = getattr(compliance, "contract_compliance", 1.0)
    import_completeness = getattr(compliance, "import_completeness", 1.0)
    stubs = getattr(compliance, "stubs_remaining", 0)
    semantic_issues = getattr(compliance, "semantic_issues", [])

    stub_penalty = max(0.0, 1.0 - stubs * 0.1)
    semantic_penalty = _compute_semantic_penalty(semantic_issues, language_id)

    composite = (
        contract_compliance * 0.4
        + import_completeness * 0.2
        + stub_penalty * 0.2
        + semantic_penalty * 0.2
    )
    return max(0.0, min(1.0, composite))


def _detect_language_mismatch(content: str, file_path: str) -> Optional[str]:
    """Detect language mismatch — e.g. Python content in non-Python files (REQ-MLT-400).

    Returns an error tag like ``"python_content_in_html"`` when a mismatch is
    detected, or ``None`` when the content appears to match its extension.
    """
    ext = Path(file_path).suffix.lower()
    name = Path(file_path).name.lower()

    # --- Python fingerprints in non-Python files ---
    if ext != ".py":
        lines = content.splitlines()
        # Find first non-blank, non-comment line
        first_code_line = ""
        for line in lines:
            stripped = line.strip()
            if stripped and not stripped.startswith("#"):
                first_code_line = stripped
                break

        # Definitive: content is ONLY `from __future__ import annotations`
        if content.strip() == "from __future__ import annotations":
            label = ext.lstrip(".") if ext else name
            return f"python_content_in_{label}"

        # Strong signal: `from __future__ import`
        for line in lines:
            if line.strip().startswith("from __future__ import"):
                label = ext.lstrip(".") if ext else name
                return f"python_content_in_{label}"

        # Strong signal: first code line is a Python import statement
        if first_code_line.startswith("import ") or first_code_line.startswith("from "):
            # Exclude languages that share `import`/`from` keywords with Python:
            #   Go — `import "pkg"` / go.mod
            #   JS/TS — ESM `import X from 'pkg'`
            #   Groovy/Gradle — `import groovy.transform.CompileStatic`
            #   Java — `import java.util.List` (.java handled by _validate_java_file)
            _IMPORT_EXCLUDE = (
                ".go", ".js", ".mjs", ".cjs", ".ts", ".tsx", ".jsx",
                ".gradle", ".groovy", ".gvy", ".gy", ".gsh", ".kts",
                ".java",
            )
            if ext not in _IMPORT_EXCLUDE and name != "go.mod":
                label = ext.lstrip(".") if ext else name
                return f"python_content_in_{label}"

        # Moderate signal: `def ` or `class ` as first code construct
        # Exclude languages where `def` and `class` are native keywords
        _DEF_CLASS_EXCLUDE = (
            ".gradle", ".groovy", ".gvy", ".gy", ".gsh", ".kts",
            ".cs",  # C# uses `class` natively
        )
        if ext not in _DEF_CLASS_EXCLUDE:
            if first_code_line.startswith("def ") or first_code_line.startswith("class "):
                label = ext.lstrip(".") if ext else name
                return f"python_content_in_{label}"

    # --- Go fingerprints in non-Go files ---
    if ext not in (".go",) and name != "go.mod":
        for line in content.splitlines():
            stripped = line.strip()
            if stripped == "package main" and ext in (".html", ".htm", ".yaml", ".yml", ".json"):
                label = ext.lstrip(".")
                return f"go_content_in_{label}"

    # --- Node.js/JavaScript fingerprints in non-JS files (REQ-NODE-202) ---
    _JS_EXTENSIONS = (".js", ".mjs", ".cjs", ".ts", ".tsx", ".jsx")
    if ext not in _JS_EXTENSIONS and ext != ".py":
        _NODE_FINGERPRINT_RE = re.compile(
            r"^\s*(?:const|let|var)\s+\w+\s*=\s*require\(", re.MULTILINE,
        )
        _MODULE_EXPORTS_RE = re.compile(
            r"^\s*module\.exports\s*=", re.MULTILINE,
        )
        if _NODE_FINGERPRINT_RE.search(content) or _MODULE_EXPORTS_RE.search(content):
            label = ext.lstrip(".") if ext else name
            return f"nodejs_content_in_{label}"

    # --- C# fingerprints in non-C# files (REQ-CS-203) ---
    _CS_EXTENSIONS = (".cs", ".csx")
    if ext not in _CS_EXTENSIONS and ext != ".py":
        # "using System;" or "using Microsoft." — C# using directives
        _CS_USING_RE = re.compile(
            r"^\s*using\s+(?:System|Microsoft)\b[\w.]*\s*;", re.MULTILINE,
        )
        # "namespace X {" or "namespace X;" (file-scoped) — C# namespace
        _CS_NAMESPACE_RE = re.compile(
            r"^\s*namespace\s+[\w.]+\s*[{;]", re.MULTILINE,
        )
        # "[assembly:" — C# assembly attribute
        _CS_ASSEMBLY_RE = re.compile(r"^\s*\[assembly:", re.MULTILINE)
        if (
            _CS_USING_RE.search(content)
            or _CS_NAMESPACE_RE.search(content)
            or _CS_ASSEMBLY_RE.search(content)
        ):
            label = ext.lstrip(".") if ext else name
            return f"csharp_content_in_{label}"

    return None


