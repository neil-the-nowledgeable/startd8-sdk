#: Extension → compiled stub patterns, loaded lazily from LanguageRegistry profiles.
_STUB_PATTERNS_CACHE: dict[str, list[re.Pattern[str]]] = {}

_STUB_EXT_TO_LANG: dict[str, str] = {
    ".java": "java", ".go": "go", ".cs": "csharp",
    ".js": "nodejs", ".mjs": "nodejs", ".cjs": "nodejs",
}


def _get_stub_patterns(suffix: str) -> list[re.Pattern[str]]:
    """Return compiled stub patterns for a file extension, cached."""
    if suffix in _STUB_PATTERNS_CACHE:
        return _STUB_PATTERNS_CACHE[suffix]
    patterns: list[re.Pattern[str]] = []
    lang_id = _STUB_EXT_TO_LANG.get(suffix)
    if lang_id:
        try:
            from startd8.languages.registry import LanguageRegistry
            LanguageRegistry.discover()
            profile = LanguageRegistry.get(lang_id)
            if profile:
                for pat_str in profile.stub_patterns:
                    try:
                        patterns.append(re.compile(pat_str))
                    except re.error:
                        pass
        except (ImportError, AttributeError):
            pass
    _STUB_PATTERNS_CACHE[suffix] = patterns
    return patterns


def _count_stubs_text(content: str, suffix: str) -> int:
    """Count stub markers in non-Python source via language-specific patterns.

    Uses the ``stub_patterns`` property from the language profile resolved by file
    extension. Returns 0 when no patterns are available.
    """
    patterns = _get_stub_patterns(suffix)
    if not patterns:
        return 0
    count = 0
    for line in content.splitlines():
        for pat in patterns:
            if pat.search(line):
                count += 1
                break  # one match per line is enough
    return count


def _validate_non_python_file(
    abs_path: Path,
    result: DiskComplianceResult,
    *,
    sibling_imports: Optional[Dict[str, Set[str]]] = None,
) -> DiskComplianceResult:
    """Lightweight validators for non-Python files (KZ-Q4).

    Prevents false-positive 1.0 scores on files like ``requirements.in``
    that contain function names instead of packages, or malformed
    Dockerfiles/YAML/JSON.
    """
    try:
        content = abs_path.read_text(encoding="utf-8", errors="replace")
    except OSError as exc:
        result.ast_valid = False
        result.error = f"read_error: {exc}"
        return result

    # REQ-MLT-400: Language mismatch detection (universal first-pass)
    mismatch = _detect_language_mismatch(content, str(abs_path))
    if mismatch:
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = f"language_mismatch:{mismatch}"
        return result

    # Universal empty-file guard — an empty generated file is never valid,
    # regardless of type (XML, .properties, unknown extensions, etc.). Code
    # validators (Go, C#) also flag this, but enforcing it here covers every
    # dispatch branch uniformly.
    if not content.strip():
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = "empty_file"
        return result

    suffix = abs_path.suffix.lower()
    name = abs_path.name.lower()

    # RUN-007 FR-5/FR-6: empty stem-named type artifact detector (Fix 2 / C-1).
    # The run-007 stubs (`export class value-model {}`) are syntactically valid
    # and carry ZERO stub markers, so the ordinary stub channel leaves the
    # disk-quality score ~0.94 and the postmortem never sees them. On match we
    # force the FAIL verdict by setting ast_valid=False / contract_compliance=0.0
    # — compute_disk_quality_score short-circuits to 0.0 only on ast_valid=False.
    # The shared predicate already excludes barrels (`export *`), function
    # modules, and name-mismatches; here we add the `.d.ts` ambient-declaration
    # exemption (Path("foo.d.ts").suffix == ".ts", so it would otherwise route
    # through the same path) and scope the check to source-code languages.
    _SOURCE_CODE_SUFFIXES = {
        ".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs", ".go", ".java", ".cs",
    }
    if suffix in _SOURCE_CODE_SUFFIXES and not name.endswith(".d.ts"):
        try:
            from startd8.element_fillability import is_empty_stem_type_artifact

            if is_empty_stem_type_artifact(str(abs_path), content):
                result.ast_valid = False
                result.contract_compliance = 0.0
                result.error = f"empty_stem_stub: {abs_path.name}"
                result.semantic_issues.append({
                    "category": "empty_stem_stub",
                    "severity": "error",
                    "message": (
                        "empty stem-named type with no members — "
                        "unfilled run-007 stub shape"
                    ),
                    "file": str(abs_path.name),
                })
                return result
        except ImportError:
            pass

    # Filename-based dispatch (before extension-based)
    if name == "go.mod":
        result = _validate_go_mod(content, result)
    elif name == "package.json":
        result = _validate_package_json(content, result)
    elif suffix == ".in" or name == "requirements.txt":
        result = _validate_requirements_file(content, result)
        # L5: Requirements-to-import cross-check (REQ-SV-601)
        if sibling_imports:
            _validate_requirements_coverage(content, sibling_imports, result)
    elif name == "dockerfile" or name.startswith("dockerfile."):
        result = _validate_dockerfile(content, result)
    elif suffix in (".html", ".htm"):
        result = _validate_html_file(content, result)
    elif suffix in (".yaml", ".yml"):
        result = _validate_yaml_file(content, result)
    elif suffix in (".js", ".mjs", ".cjs", ".ts", ".tsx", ".jsx"):
        result = _validate_js_file(content, result, file_path=str(abs_path))
    elif suffix == ".go":
        result = _validate_go_file(content, result, file_path=str(abs_path))
    elif suffix == ".json":
        result = _validate_json_file(content, result, filename=abs_path.name)
    elif suffix == ".java":
        result = _validate_java_file(content, result)
    elif name in ("build.gradle", "build.gradle.kts"):
        result = _validate_build_gradle(content, result)
    elif suffix == ".cs":
        result = _validate_csharp_file(content, result)
    elif suffix == ".csproj":
        result = _validate_csproj_file(content, result)
    elif suffix == ".sln":
        result = _validate_sln_file(content, result)
    elif suffix == ".xml":
        result = _validate_xml_file(content, result)

    return result


def _validate_csharp_file(
    content: str,
    result: DiskComplianceResult,
) -> DiskComplianceResult:
    """Validate C# source file structure (REQ-CS-200).

    Uses tree-sitter-c-sharp for per-file syntax validation when available
    (~5ms, in-process). Falls back to text-based heuristics.
    """
    issues: list = []

    if not content.strip():
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = "empty_file"
        return result

    # Python fingerprint guard (line-anchored — substring scans false-flag
    # valid C# like an identifier named `self`/`def` or text in a string).
    _cs_fp = detect_python_contamination(content, "csharp")
    if _cs_fp:
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = f"Python fingerprint in .cs file: {_cs_fp!r}"
        return result

    # Try tree-sitter first (REQ-CS-200 Tier 1)
    try:
        from startd8.languages.csharp_parser import parse_csharp
        parse_result = parse_csharp(content)
        if parse_result.parser_used == "tree_sitter":
            if parse_result.has_error:
                result.ast_valid = False
                result.contract_compliance = 0.0
                result.error = "csharp_syntax_error"
                return result
            # Structural checks on parsed tree
            kinds = {e.kind for e in parse_result.elements}
            has_type = kinds & {"class", "interface", "struct", "record", "enum"}
            if not has_type and not parse_result.namespace:
                issues.append({
                    "category": "csharp_structure",
                    "severity": "warning",
                    "message": "no type declaration found (class/interface/struct/record/enum)",
                })
                result.contract_compliance = max(
                    0.0, result.contract_compliance - 0.3,
                )
            # REQ-KZ-CS-500b: file-scoped namespace hint
            issues.extend(_csharp_namespace_style_issues(content))
            # REQ-KZ-CS-200: C# semantic checks (empty catches, Console.WriteLine,
            # async void, missing access modifiers, etc.) — feed into disk quality score
            try:
                from startd8.validators.csharp_semantic_checks import (
                    run_csharp_semantic_checks,
                )
                for sem_issue in run_csharp_semantic_checks(content):
                    issues.append({
                        "category": sem_issue.check,
                        "severity": sem_issue.severity,
                        "message": sem_issue.message,
                    })
            except ImportError:
                pass  # csharp_semantic_checks not available
            if issues:
                result.semantic_issues = issues
            return result
    except ImportError:
        pass  # Fall through to text-based

    # Text-based fallback (REQ-CS-200 Tier 2)
    depth = 0
    for ch in content:
        if ch == "{":
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth < 0:
                result.ast_valid = False
                result.contract_compliance = 0.5
                result.error = "unbalanced_braces"
                return result
    if depth > 0:
        result.ast_valid = False
        result.contract_compliance = 0.5
        result.error = "unbalanced_braces"
        return result

    has_namespace = bool(re.search(r"\bnamespace\s+\w+", content))
    has_using = bool(re.search(r"^using\s+[\w.]+;", content, re.MULTILINE))
    has_type_decl = bool(
        re.search(r"\b(?:class|interface|struct|record|enum)\s+\w+", content),
    )

    if not has_namespace and not has_using and not has_type_decl:
        result.ast_valid = False
        result.contract_compliance = 0.3
        result.error = "no_csharp_keywords"
        return result

    # REQ-KZ-CS-500b: file-scoped namespace hint
    ns_issues = _csharp_namespace_style_issues(content)
    if ns_issues:
        result.semantic_issues.extend(ns_issues)

    # REQ-KZ-CS-200: run the same C# semantic checks here so the text-fallback
    # path produces the same semantic_issues as the tree-sitter path above —
    # otherwise SQL-injection / Console.WriteLine / etc. are only detected when
    # tree-sitter is installed.
    try:
        from startd8.validators.csharp_semantic_checks import (
            run_csharp_semantic_checks,
        )
        for sem_issue in run_csharp_semantic_checks(content):
            result.semantic_issues.append({
                "category": sem_issue.check,
                "severity": sem_issue.severity,
                "message": sem_issue.message,
            })
    except ImportError:
        pass  # csharp_semantic_checks not available

    # Text fallback passes — cap at 0.8 (no tree-sitter confirmation)
    result.contract_compliance = min(result.contract_compliance, 0.8)

    # Stub counting (Criterion 4 — postmortem scoring parity)
    result.stubs_remaining = _count_stubs_text(content, ".cs")

    return result


def _csharp_namespace_style_issues(content: str) -> list:
    """Return info-severity issues for block-scoped namespaces (REQ-KZ-CS-500b)."""
    block_match = re.search(r"^namespace\s+[\w.]+\s*\{", content, re.MULTILINE)
    if block_match:
        line = content[: block_match.start()].count("\n") + 1
        return [{
            "category": "block_scoped_namespace",
            "severity": "info",
            "message": (
                "Block-scoped namespace detected — prefer file-scoped "
                "syntax (`namespace Foo.Bar;`) for net8.0+ targets"
            ),
            "line": line,
        }]
    return []


def _validate_csproj_file(
    content: str,
    result: DiskComplianceResult,
) -> DiskComplianceResult:
    """Validate .csproj file structure (REQ-CS-201).

    Checks XML well-formedness, <Project> root, <TargetFramework>,
    and <PackageReference> attributes.
    """
    if not content.strip():
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = "empty_file"
        return result

    # XML well-formedness
    try:
        import xml.etree.ElementTree as ET
        root = ET.fromstring(content)
    except ET.ParseError as exc:
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = f"xml_parse_error: {exc}"
        return result

    # Strip namespace for tag matching
    tag = root.tag.split("}")[-1] if "}" in root.tag else root.tag
    if tag != "Project":
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = "missing_project_root"
        return result

    # Check Sdk attribute
    sdk = root.get("Sdk", "")
    if not sdk:
        result.semantic_issues.append({
            "category": "csproj_structure",
            "severity": "warning",
            "message": "missing Sdk attribute on <Project> element",
        })

    # Check TargetFramework(s) — search all PropertyGroup children
    has_target_framework = False
    for elem in root.iter():
        etag = elem.tag.split("}")[-1] if "}" in elem.tag else elem.tag
        if etag in ("TargetFramework", "TargetFrameworks"):
            has_target_framework = True
            break

    if not has_target_framework:
        result.contract_compliance = 0.3
        result.semantic_issues.append({
            "category": "csproj_structure",
            "severity": "error",
            "message": "missing <TargetFramework> or <TargetFrameworks> element",
        })

    # Check PackageReference entries have required attributes
    for elem in root.iter():
        etag = elem.tag.split("}")[-1] if "}" in elem.tag else elem.tag
        if etag == "PackageReference":
            inc = elem.get("Include", "")
            ver = elem.get("Version", "")
            if not inc:
                result.semantic_issues.append({
                    "category": "csproj_structure",
                    "severity": "warning",
                    "message": "PackageReference missing Include attribute",
                })

    # REQ-KZ-CS-200: run csproj-specific semantic checks (e.g.
    # missing_nullable_in_csproj) — run_csharp_semantic_checks dispatches by
    # file_path suffix, so passing result.file_path routes to the .csproj branch.
    try:
        from startd8.validators.csharp_semantic_checks import (
            run_csharp_semantic_checks,
        )
        for sem_issue in run_csharp_semantic_checks(
            content, file_path=result.file_path,
        ):
            result.semantic_issues.append({
                "category": sem_issue.check,
                "severity": sem_issue.severity,
                "message": sem_issue.message,
            })
    except ImportError:
        pass  # csharp_semantic_checks not available

    return result


def _validate_sln_file(
    content: str,
    result: DiskComplianceResult,
) -> DiskComplianceResult:
    """Validate .sln file structure (REQ-CS-202).

    Checks for solution header, project entries, and balanced
    Project/EndProject blocks.
    """
    if not content.strip():
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = "empty_file"
        return result

    # Check solution header
    if "Microsoft Visual Studio Solution File" not in content:
        result.ast_valid = False
        result.contract_compliance = 0.0
        result.error = "missing_solution_header"
        return result

    # Count Project/EndProject pairs
    project_count = content.count("Project(")
    end_project_count = content.count("EndProject")

    if project_count == 0:
        result.contract_compliance = 0.3
        result.semantic_issues.append({
            "category": "sln_structure",
            "severity": "warning",
            "message": "no Project entries found in solution file",
        })
    elif project_count != end_project_count:
        result.contract_compliance = 0.5
        result.semantic_issues.append({
            "category": "sln_structure",
            "severity": "error",
            "message": (
                f"unbalanced Project/EndProject: "
                f"{project_count} Project( vs {end_project_count} EndProject"
            ),
        })

    return result


def _validate_requirements_file(
    content: str,
    result: DiskComplianceResult,
) -> DiskComplianceResult:
    """Validate pip requirements format.

    Each non-empty, non-comment line must look like a valid package
    specifier: alphanumeric with hyphens/underscores/dots, optionally
    followed by extras or version constraints.  Lines like ``setCurrency``
    or ``browseProduct`` that are Python identifiers but not on PyPI are
    caught by checking for CamelCase or known non-package patterns.
    """
    # PEP 508 package name: letter/digit, hyphens, underscores, dots
    _PKG_RE = re.compile(
        r"^[A-Za-z0-9]([A-Za-z0-9._-]*[A-Za-z0-9])?(\[.*\])?\s*(~=|==|!=|>=|<=|>|<|===)?",
    )
    # Bare camelCase identifier with no version specifier — likely a function name
    _CAMEL_BARE_RE = re.compile(r"^[a-z]+[A-Z][a-zA-Z]*$")

    issues: list = []
    lines = [
        line.strip() for line in content.splitlines()
        if line.strip() and not line.strip().startswith("#")
        and not line.strip().startswith("-")  # pip flags like -e, --index-url
    ]

    for line in lines:
        # Strip inline comments
        spec = line.split("#")[0].strip()
        if not spec:
            continue
        if _CAMEL_BARE_RE.match(spec):
            issues.append(f"camelCase identifier (not a package): {spec}")
        elif not _PKG_RE.match(spec):
            issues.append(f"invalid package specifier: {spec}")

    if issues:
        # Scale contract_compliance by fraction of valid lines
        valid_count = len(lines) - len(issues)
        result.contract_compliance = max(0.0, valid_count / len(lines)) if lines else 0.0
        result.semantic_issues.extend(issues)
        result.error = f"{len(issues)} invalid package specifier(s)"

    return result


def _digest_looks_fabricated(digest: str) -> bool:
    """Heuristic: detect fabricated SHA256 digests (REQ-SV2-100).

    Real SHA256 digests have high Shannon entropy (~3.9 bits/char).
    LLM-fabricated digests often contain sequential hex nybble patterns
    or low-entropy repeated characters.
    """
    from math import log2

    d = digest.lower()
    length = len(d)
    if length == 0:
        return True

    # 1. Shannon entropy — real digests average ~3.9 bits/char
    freq: dict[str, int] = {}
    for c in d:
        freq[c] = freq.get(c, 0) + 1
    entropy = -sum((n / length) * log2(n / length) for n in freq.values())
    if entropy < 3.0:
        return True

    # 2. Nybble-level sequential detection — LLMs produce digests like
    #    "0d1e2f3a4b5c6d7e" where even-position hex chars count 0,1,2,...
    #    Check both even and odd nybble positions for runs of 8+ with
    #    constant diff (mod 16).
    nybbles = [int(c, 16) for c in d]
    for offset in (0, 1):
        stripe = nybbles[offset::2]
        if len(stripe) < 3:
            continue
        prev_diff = (stripe[1] - stripe[0]) % 16
        run = 2
        for i in range(2, len(stripe)):
            cur_diff = (stripe[i] - stripe[i - 1]) % 16
            if cur_diff == prev_diff:
                run += 1
                if run >= 8:
                    return True
            else:
                prev_diff = cur_diff
                run = 2

    return False


def _validate_dockerfile(
    content: str,
    result: DiskComplianceResult,
) -> DiskComplianceResult:
    """Validate that a Dockerfile has required directives and valid digests."""
    lines = content.splitlines()
    directives_found: set[str] = set()
    for lineno, line in enumerate(lines, 1):
        stripped = line.strip()
        if stripped and not stripped.startswith("#"):
            match = re.match(r"^([A-Z]+)\s", stripped)
            if match:
                directives_found.add(match.group(1))

        # L3: SHA256 digest validation (REQ-SV-401)
        # L3+: Digest plausibility (REQ-SV2-100)
        if stripped.upper().startswith("FROM"):
            for digest_match in re.findall(r"@sha256:([0-9a-fA-F]*)", stripped):
                if len(digest_match) != 64:
                    result.semantic_issues.append({
                        "category": "dockerfile_digest",
                        "severity": "error",
                        "message": (
                            f"Truncated SHA256 digest: {len(digest_match)} chars "
                            f"(expected 64)"
                        ),
                        "line": lineno,
                        "symbol": (
                            f"sha256:{digest_match[:16]}..."
                            if digest_match
                            else "sha256:<empty>"
                        ),
                    })
                elif _digest_looks_fabricated(digest_match):
                    result.semantic_issues.append({
                        "category": "dockerfile_digest_fabricated",
                        "severity": "error",
                        "message": (
                            "SHA256 digest appears fabricated "
                            "(low entropy or sequential pattern)"
                        ),
                        "line": lineno,
                        "symbol": f"sha256:{digest_match[:16]}...",
                    })

    required = {"FROM"}
    # A valid Dockerfile needs at least FROM
    missing = required - directives_found
    if missing:
        result.ast_valid = False
        result.error = f"missing Dockerfile directive(s): {', '.join(sorted(missing))}"
        result.contract_compliance = 0.0
    elif not (directives_found & {"CMD", "ENTRYPOINT"}):
        # Warning: no entrypoint defined
        result.semantic_issues.append("no CMD or ENTRYPOINT directive")
        result.contract_compliance = 0.8

    return result


