export type Capability = z.infer<typeof CapabilitySchema>;
export type Outcome = z.infer<typeof OutcomeSchema>;
export type Metric = z.infer<typeof MetricSchema>;
export type Differentiator = z.infer<typeof DifferentiatorSchema>;
export type ValueProp = z.infer<typeof ValuePropSchema>;
export type Artifact = z.infer<typeof ArtifactSchema>;
export type AiCall = z.infer<typeof AiCallSchema>;

// ─────────────────────────────────────────────────────────────────────────────
// ValueModel — composite aggregate type
//
// The complete in-memory representation of a profile's value model.
// API routes return this shape; client state is typed against it.
// ─────────────────────────────────────────────────────────────────────────────

export type ValueModel = {
  /** Core profile record */
  profile: Profile;

  /** All proof points belonging to this profile */
  proofPoints: ProofPoint[];

  /** All capabilities belonging to this profile */
  capabilities: Capability[];

  /** All outcomes belonging to this profile */
  outcomes: Outcome[];

  /** All metrics belonging to this profile */
  metrics: Metric[];

  /** All differentiators belonging to this profile */
  differentiators: Differentiator[];

  /** All value propositions belonging to this profile */
  valueProps: ValueProp[];

  /** All artifacts belonging to this profile */
  artifacts: Artifact[];
};

// ─────────────────────────────────────────────────────────────────────────────
// ValueModelSchema — Zod schema for the full aggregate (useful for API
// response validation in tests and client-side fetch wrappers)
// ─────────────────────────────────────────────────────────────────────────────

export const ValueModelSchema = z.object({
  profile: ProfileSchema,
  proofPoints: z.array(ProofPointSchema),
  capabilities: z.array(CapabilitySchema),
  outcomes: z.array(OutcomeSchema),
  metrics: z.array(MetricSchema),
  differentiators: z.array(DifferentiatorSchema),
  valueProps: z.array(ValuePropSchema),
  artifacts: z.array(ArtifactSchema),
});