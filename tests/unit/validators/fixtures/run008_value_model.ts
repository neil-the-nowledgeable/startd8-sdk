/**
 * lib/value-model.ts
 *
 * Single canonical Zod schema definitions mirroring all Prisma Value Model
 * entities. These schemas are the shared contract between API routes, server
 * actions, and client components.
 *
 * Field mapping conventions:
 *   - Prisma String   → z.string()
 *   - Prisma String?  → z.string().nullable()
 *   - Prisma DateTime → z.string().datetime()  (ISO-8601 wire format)
 *   - Prisma Json     → z.unknown()
 *   - @default fields → .optional() (server-generated, not required on input)
 *   - @id fields      → z.string()
 */

import { z } from "zod";

// ─────────────────────────────────────────────────────────────────────────────
// ProfileSchema
// Singleton record representing the professional whose value is being modelled.
// ─────────────────────────────────────────────────────────────────────────────
export const ProfileSchema = z.object({
  /** Primary key — CUID generated by Prisma */
  id: z.string(),

  /** Full display name */
  name: z.string(),

  /** Professional title or headline */
  title: z.string().nullable(),

  /** Short bio / executive summary */
  bio: z.string().nullable(),

  /** Primary contact email */
  email: z.string().email().nullable(),

  /** LinkedIn profile URL */
  linkedinUrl: z.string().url().nullable(),

  /** Personal or company website URL */
  websiteUrl: z.string().url().nullable(),

  /** Geographic location string (e.g. "San Francisco, CA") */
  location: z.string().nullable(),

  /** Years of total professional experience */
  yearsOfExperience: z.number().int().nullable(),

  /** Primary industry or sector */
  industry: z.string().nullable(),

  /** Freeform JSON blob for extensible metadata */
  metadata: z.unknown().nullable(),

  /** ISO-8601 creation timestamp */
  createdAt: z.string().datetime(),

  /** ISO-8601 last-update timestamp */
  updatedAt: z.string().datetime(),
});

// ─────────────────────────────────────────────────────────────────────────────
// ProofPointSchema
// Concrete, verifiable evidence items supporting claims in the value model.
// ─────────────────────────────────────────────────────────────────────────────
export const ProofPointSchema = z.object({
  id: z.string(),

  /** Foreign key → Profile */
  profileId: z.string(),

  /**
   * Category of proof: e.g. "achievement", "testimonial", "certification",
   * "publication", "award", "case_study"
   */
  category: z.string(),

  /** Human-readable title */
  title: z.string(),

  /** Detailed description of the proof point */
  description: z.string().nullable(),

  /** Quantified impact statement (e.g. "Reduced churn by 22%") */
  impact: z.string().nullable(),

  /** Source URL, citation, or reference */
  source: z.string().nullable(),

  /** Calendar date when this proof point occurred (ISO date string) */
  occurredAt: z.string().datetime().nullable(),

  /** Whether this proof point has been independently verified */
  verified: z.boolean(),

  /** Freeform JSON blob for extensible metadata */
  metadata: z.unknown().nullable(),

  createdAt: z.string().datetime(),
  updatedAt: z.string().datetime(),
});

// ─────────────────────────────────────────────────────────────────────────────
// CapabilitySchema
// A skill, competency, or area of expertise.
// ─────────────────────────────────────────────────────────────────────────────
export const CapabilitySchema = z.object({
  id: z.string(),

  /** Foreign key → Profile */
  profileId: z.string(),

  /** Capability name (e.g. "System Design", "Revenue Operations") */
  name: z.string(),

  /** Broader grouping (e.g. "Technical", "Leadership", "Domain") */
  category: z.string().nullable(),

  /** Narrative description of the capability */
  description: z.string().nullable(),

  /**
   * Proficiency level: "beginner" | "intermediate" | "advanced" | "expert"
   * Stored as string to allow future extension without migration.
   */
  proficiencyLevel: z.string().nullable(),

  /** Number of years this capability has been practiced */
  yearsOfExperience: z.number().int().nullable(),

  /** Whether this is highlighted as a core/primary capability */
  isPrimary: z.boolean(),

  /** Display order for UI rendering */
  sortOrder: z.number().int(),

  /** Freeform JSON blob for extensible metadata */
  metadata: z.unknown().nullable(),

  createdAt: z.string().datetime(),
  updatedAt: z.string().datetime(),
});

// ─────────────────────────────────────────────────────────────────────────────
// OutcomeSchema
// A result, deliverable, or impact produced — the "so what" of the value model.
// ─────────────────────────────────────────────────────────────────────────────
export const OutcomeSchema = z.object({
  id: z.string(),

  /** Foreign key → Profile */
  profileId: z.string(),

  /** Short title of the outcome */
  title: z.string(),

  /** Detailed description */
  description: z.string().nullable(),

  /**
   * Outcome type: "business", "technical", "personal", "team", "customer"
   */
  outcomeType: z.string().nullable(),

  /** Quantified result where applicable (e.g. "$2M ARR added") */
  quantifiedResult: z.string().nullable(),

  /** Timeframe over which the outcome was achieved */
  timeframe: z.string().nullable(),

  /** Organisation or client context */
  context: z.string().nullable(),

  /** Display order for UI rendering */
  sortOrder: z.number().int(),

  /** Freeform JSON blob for extensible metadata */
  metadata: z.unknown().nullable(),

  createdAt: z.string().datetime(),
  updatedAt: z.string().datetime(),
});

// ─────────────────────────────────────────────────────────────────────────────
// MetricSchema
// A quantified measurement supporting an outcome or capability claim.
// ─────────────────────────────────────────────────────────────────────────────
export const MetricSchema = z.object({
  id: z.string(),

  /** Foreign key → Profile */
  profileId: z.string(),

  /** Optional foreign key → Outcome (metric may belong to an outcome) */
  outcomeId: z.string().nullable(),

  /** Metric label (e.g. "Monthly Active Users", "Net Revenue Retention") */
  name: z.string(),

  /** Numeric value */
  value: z.number(),

  /** Unit of measure (e.g. "%", "USD", "count", "x") */
  unit: z.string().nullable(),

  /**
   * Direction of improvement: "increase" | "decrease" | "maintain"
   * Allows rendering arrows/indicators correctly.
   */
  direction: z.string().nullable(),

  /** Baseline value before the change (for delta metrics) */
  baselineValue: z.number().nullable(),

  /** Target or goal value */
  targetValue: z.number().nullable(),

  /** Time period the metric covers (e.g. "Q3 2023", "FY2022") */
  period: z.string().nullable(),

  /** Category grouping for the metric (e.g. "Revenue", "Efficiency") */
  category: z.string().nullable(),

  /** Freeform JSON blob for extensible metadata */
  metadata: z.unknown().nullable(),

  createdAt: z.string().datetime(),
  updatedAt: z.string().datetime(),
});

// ─────────────────────────────────────────────────────────────────────────────
